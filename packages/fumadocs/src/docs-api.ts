function isPlainObject(value: unknown): value is Record<string, unknown> {
  return typeof value === "object" && value !== null && !Array.isArray(value);
}

function normalizeAgentFeedbackContext(value: unknown): DocsAgentFeedbackContext | undefined {
  if (!isPlainObject(value)) return undefined;

  const context: DocsAgentFeedbackContext = {};
  if (typeof value.page === "string") context.page = value.page;
  if (typeof value.url === "string") context.url = value.url;
  if (typeof value.slug === "string") context.slug = value.slug;
  if (typeof value.locale === "string") context.locale = value.locale;
  if (typeof value.source === "string") context.source = value.source;

  return Object.keys(context).length > 0 ? context : undefined;
}

async function parseAgentFeedbackData(
  request: Request,
): Promise<{ ok: true; data: DocsAgentFeedbackData } | { ok: false; response: Response }> {
  let body: unknown;

  try {
    body = await request.json();
  } catch {
    return {
      ok: false,
      response: Response.json({ error: "Agent feedback body must be valid JSON" }, { status: 400 }),
    };
  }

  if (!isPlainObject(body)) {
    return {
      ok: false,
      response: Response.json({ error: "Agent feedback body must be an object" }, { status: 400 }),
    };
  }

  if (!isPlainObject(body.payload)) {
    return {
      ok: false,
      response: Response.json(
        { error: "Agent feedback body must include a payload object" },
        { status: 400 },
      ),
    };
  }

  return {
    ok: true,
    data: {
      context: normalizeAgentFeedbackContext(body.context),
      payload: body.payload,
    },
  };
}

function validateAgentFeedbackPayload(
  value: unknown,
  schema: Record<string, unknown>,
  valuePath = "payload",
): string | undefined {
  const schemaType = typeof schema.type === "string" ? schema.type : undefined;

  if (Array.isArray(schema.enum) && !schema.enum.some((entry) => Object.is(entry, value))) {
    return `${valuePath} must be one of the configured enum values`;
  }

  if (schemaType === "object" || (!schemaType && (schema.properties || schema.required))) {
    if (!isPlainObject(value)) return `${valuePath} must be an object`;

    const properties = isPlainObject(schema.properties) ? schema.properties : {};
    const required = Array.isArray(schema.required)
      ? schema.required.filter((entry): entry is string => typeof entry === "string")
      : [];

    for (const key of required) {
      if (!(key in value)) return `${valuePath}.${key} is required`;
    }

    if (schema.additionalProperties === false) {
      for (const key of Object.keys(value)) {
        if (!(key in properties)) return `${valuePath}.${key} is not allowed`;
      }
    }

    for (const [key, propertySchema] of Object.entries(properties)) {
      if (!(key in value)) continue;
      if (!isPlainObject(propertySchema)) continue;

      const error = validateAgentFeedbackPayload(value[key], propertySchema, `${valuePath}.${key}`);
      if (error) return error;
    }

    return undefined;
  }

  if (schemaType === "array") {
    if (!Array.isArray(value)) return `${valuePath} must be an array`;
    if (!isPlainObject(schema.items)) return undefined;

    for (const [index, item] of value.entries()) {
      const error = validateAgentFeedbackPayload(item, schema.items, `${valuePath}[${index}]`);
      if (error) return error;
    }

    return undefined;
  }

  if (schemaType === "string") {
    if (typeof value !== "string") return `${valuePath} must be a string`;

    if (typeof schema.minLength === "number" && value.length < schema.minLength) {
      return `${valuePath} must be at least ${schema.minLength} characters`;
    }

    if (typeof schema.maxLength === "number" && value.length > schema.maxLength) {
      return `${valuePath} must be at most ${schema.maxLength} characters`;
    }

    return undefined;
  }

  if (schemaType === "number") {
    if (typeof value !== "number" || !Number.isFinite(value))
      return `${valuePath} must be a number`;

    if (typeof schema.minimum === "number" && value < schema.minimum) {
      return `${valuePath} must be >= ${schema.minimum}`;
    }

    if (typeof schema.maximum === "number" && value > schema.maximum) {
      return `${valuePath} must be <= ${schema.maximum}`;
    }

    return undefined;
  }

  if (schemaType === "boolean") {
    if (typeof value !== "boolean") return `${valuePath} must be a boolean`;
    return undefined;
  }

  return undefined;
}

function readEntry(root: string): string {
  for (const ext of FILE_EXTS) {
    const configPath = path.join(root, `docs.config.${ext}`);
    if (fs.existsSync(configPath)) {
      try {
        const content = fs.readFileSync(configPath, "utf-8");
        const match = content.match(/entry\s*:\s*["']([^"']+)["']/);
        if (match) return match[1];
      } catch {
        // fall through
      }
    }
  }
  return "docs";
}

function readI18nConfig(root: string): DocsI18nConfig | null {
  for (const ext of FILE_EXTS) {
    const configPath = path.join(root, `docs.config.${ext}`);
    if (!fs.existsSync(configPath)) continue;
    try {
      const content = fs.readFileSync(configPath, "utf-8");
      if (!content.includes("i18n")) continue;

      const localesMatch = content.match(/i18n\s*:\s*\{[\s\S]*?locales\s*:\s*\[([^\]]+)\]/);
      if (!localesMatch) continue;

      const locales = localesMatch[1]
        .split(",")
        .map((l) => l.trim().replace(/^['"`]|['"`]$/g, ""))
        .filter(Boolean);
      if (locales.length === 0) continue;

      const defaultLocaleMatch = content.match(
        /i18n\s*:\s*\{[\s\S]*?defaultLocale\s*:\s*["']([^"']+)["']/,
      );

      return {
        locales,
        defaultLocale: defaultLocaleMatch?.[1],
      };
    } catch {
      // fall through
    }
  }
  return null;
}

/**
 * Read AI config from docs.config by parsing the file for the `ai` block.
 * This avoids importing the config (which may use JSX/React).
 */
function readAIConfig(root: string): AIOptions {
  for (const ext of FILE_EXTS) {
    const configPath = path.join(root, `docs.config.${ext}`);
    if (fs.existsSync(configPath)) {
      try {
        const content = fs.readFileSync(configPath, "utf-8");

        // Check if AI is enabled
        if (!content.includes("ai:") && !content.includes("ai :")) {
          return {};
        }

        const enabledMatch = content.match(/ai\s*:\s*\{[^}]*enabled\s*:\s*(true|false)/s);
        if (enabledMatch && enabledMatch[1] === "false") return {};

        const modelMatch = content.match(/ai\s*:\s*\{[^}]*model\s*:\s*["']([^"']+)["']/s);
        const baseUrlMatch = content.match(/ai\s*:\s*\{[^}]*baseUrl\s*:\s*["']([^"']+)["']/s);
        // Match `apiKey: process.env.SOME_VAR` and resolve it at runtime
        const apiKeyMatch = content.match(/ai\s*:\s*\{[^}]*apiKey\s*:\s*process\.env\.(\w+)/s);
        const maxResultsMatch = content.match(/ai\s*:\s*\{[^}]*maxResults\s*:\s*(\d+)/s);
        const useMcpMatch = content.match(/ai\s*:\s*\{[^}]*useMcp\s*:\s*(true|false)/s);
        const systemPromptMatch = content.match(
          /ai\s*:\s*\{[^}]*systemPrompt\s*:\s*["'`]([^"'`]+)["'`]/s,
        );
        const packageNameMatch = content.match(
          /ai\s*:\s*\{[^}]*packageName\s*:\s*["'`]([^"'`]+)["'`]/s,
        );
        const docsUrlMatch = content.match(/ai\s*:\s*\{[^}]*docsUrl\s*:\s*["'`]([^"'`]+)["'`]/s);

        return {
          enabled: true,
          model: modelMatch?.[1],
          baseUrl: baseUrlMatch?.[1],
          apiKey: apiKeyMatch?.[1] ? process.env[apiKeyMatch[1]] : undefined,
          maxResults: maxResultsMatch ? parseInt(maxResultsMatch[1], 10) : undefined,
          useMcp: useMcpMatch ? useMcpMatch[1] === "true" : undefined,
          systemPrompt: systemPromptMatch?.[1],
          packageName: packageNameMatch?.[1],
          docsUrl: docsUrlMatch?.[1],
        };
      } catch {
        // fall through
      }
    }
  }
  return {};
}

function readMcpConfig(root: string): boolean | DocsMcpConfig | undefined {
  for (const ext of FILE_EXTS) {
    const configPath = path.join(root, `docs.config.${ext}`);
    if (!fs.existsSync(configPath)) continue;

    try {
      const content = fs.readFileSync(configPath, "utf-8");
      const sanitized = stripCommentsAndStrings(content);
      const configObject = extractRootConfigObject(content, sanitized);
      const scopedContent = configObject?.content ?? content;
      const scopedSanitized = configObject?.sanitized ?? sanitized;

      const booleanValue = readTopLevelBoolean(scopedSanitized, "mcp");
      if (booleanValue !== undefined) return booleanValue;

      const block = extractObjectLiteral(scopedContent, scopedSanitized, "mcp");
      if (!block) continue;

      return {
        enabled: readBooleanFromBlock(block, "enabled"),
        route: readStringFromBlock(block, "route"),
        name: readStringFromBlock(block, "name"),
        version: readStringFromBlock(block, "version"),
        tools: {
          listPages: readBooleanFromBlock(block, "listPages"),
          readPage: readBooleanFromBlock(block, "readPage"),
          searchDocs: readBooleanFromBlock(block, "searchDocs"),
          getNavigation: readBooleanFromBlock(block, "getNavigation"),
        },
      };
    } catch {
      // fall through
    }
  }

  return undefined;
}

function readStringFromBlock(block: string, key: string): string | undefined {
  const match = block.match(new RegExp(`${key}\\s*:\\s*["']([^"']+)["']`));
  return match?.[1];
}

function readBooleanFromBlock(block: string, key: string): boolean | undefined {
  const match = block.match(new RegExp(`${key}\\s*:\\s*(true|false)`));
  return match ? match[1] === "true" : undefined;
}

function extractObjectLiteral(content: string, sanitized: string, key: string): string | undefined {
  const keyIndex = findTopLevelPropertyIndex(sanitized, key);
  if (keyIndex === -1) return undefined;

  const colonIndex = sanitized.indexOf(":", keyIndex + key.length);
  if (colonIndex === -1) return undefined;

  const braceStart = sanitized.indexOf("{", colonIndex);
  if (braceStart === -1) return undefined;

  let depth = 0;

  for (let index = braceStart; index < sanitized.length; index += 1) {
    const char = sanitized[index];

    if (char === "{") {
      depth += 1;
      continue;
    }

    if (char !== "}") continue;

    depth -= 1;
    if (depth === 0) {
      return content.slice(braceStart + 1, index);
    }
  }

  return undefined;
}

function readTopLevelBoolean(content: string, key: string): boolean | undefined {
  const keyIndex = findTopLevelPropertyIndex(content, key);
  if (keyIndex === -1) return undefined;

  const match = content.slice(keyIndex + key.length).match(/^\s*:\s*(true|false)\b/);
  if (!match) return undefined;
  return match[1] === "true";
}

function findTopLevelPropertyIndex(content: string, key: string): number {
  let depth = 0;

  for (let index = 0; index < content.length; index += 1) {
    const char = content[index];

    if (char === "{") {
      depth += 1;
      continue;
    }

    if (char === "}") {
      depth = Math.max(0, depth - 1);
      continue;
    }

    if (depth !== 0) continue;
    if (!content.startsWith(key, index)) continue;

    const previous = index === 0 ? "" : content[index - 1];
    if (previous && /[\w$]/.test(previous)) continue;

    const next = content[index + key.length] ?? "";
    if (next && /[\w$]/.test(next)) continue;

    const afterKey = content.slice(index + key.length);
    if (!afterKey.match(/^\s*:/)) continue;

    return index;
  }

  return -1;
}

function extractRootConfigObject(
  content: string,
  sanitized: string,
): { content: string; sanitized: string } | undefined {
  const defineDocsIndex = sanitized.indexOf("defineDocs");
  const exportDefaultIndex = sanitized.indexOf("export default");

  let braceStart = -1;

  if (defineDocsIndex !== -1) {
    const parenIndex = sanitized.indexOf("(", defineDocsIndex);
    if (parenIndex !== -1) {
      braceStart = sanitized.indexOf("{", parenIndex);
    }
  }

  if (braceStart === -1 && exportDefaultIndex !== -1) {
    braceStart = sanitized.indexOf("{", exportDefaultIndex);
  }

  if (braceStart === -1) return undefined;

  let depth = 0;

  for (let index = braceStart; index < sanitized.length; index += 1) {
    const char = sanitized[index];

    if (char === "{") {
      depth += 1;
      continue;
    }

    if (char !== "}") continue;

    depth -= 1;
    if (depth === 0) {
      return {
        content: content.slice(braceStart + 1, index),
        sanitized: sanitized.slice(braceStart + 1, index),
      };
    }
  }

  return undefined;
}

function stripCommentsAndStrings(content: string): string {
  let result = "";
  let index = 0;
  let state:
    | "normal"
    | "singleQuote"
    | "doubleQuote"
    | "template"
    | "lineComment"
    | "blockComment" = "normal";

  while (index < content.length) {
    const char = content[index];
    const next = content[index + 1];

    if (state === "lineComment") {
      if (char === "\n") {
        state = "normal";
        result += "\n";
      } else {
        result += " ";
      }
      index += 1;
      continue;
    }

    if (state === "blockComment") {
      if (char === "*" && next === "/") {
        result += "  ";
        index += 2;
        state = "normal";
      } else {
        result += char === "\n" ? "\n" : " ";
        index += 1;
      }
      continue;
    }

    if (state === "singleQuote" || state === "doubleQuote" || state === "template") {
      const quote = state === "singleQuote" ? "'" : state === "doubleQuote" ? '"' : "`";

      if (char === "\\") {
        result += " ";
        if (next) result += next === "\n" ? "\n" : " ";
        index += 2;
        continue;
      }

      result += char === "\n" ? "\n" : " ";

      if (char === quote) {
        state = "normal";
      }

      index += 1;
      continue;
    }

    if (char === "/" && next === "/") {
      result += "  ";
      index += 2;
      state = "lineComment";
      continue;
    }

    if (char === "/" && next === "*") {
      result += "  ";
      index += 2;
      state = "blockComment";
      continue;
    }

    if (char === "'") {
      result += " ";
      index += 1;
      state = "singleQuote";
      continue;
    }

    if (char === '"') {
      result += " ";
      index += 1;
      state = "doubleQuote";
      continue;
    }

    if (char === "`") {
      result += " ";
      index += 1;
      state = "template";
      continue;
    }

    result += char;
    index += 1;
  }

  return result;
}

function resolveAgentMdxContent(content: string, audience: "human" | "agent"): string {
  const lines = content.split("\n");
  const output: string[] = [];
  let fenceMarker: string | null = null;
  let agentDepth = 0;

  for (const line of lines) {
    const trimmed = line.trim();
    const fenceMatch = trimmed.match(/^(`{3,}|~{3,})/);

    if (fenceMatch) {
      if (!fenceMarker) {
        fenceMarker = fenceMatch[1];
      } else if (trimmed.startsWith(fenceMarker)) {
        fenceMarker = null;
      }

      if (audience === "agent" || agentDepth === 0) {
        output.push(line);
      }
      continue;
    }

    if (!fenceMarker) {
      if (/^<Agent(?:\s[^>]*)?\/>$/.test(trimmed)) {
        continue;
      }

      const singleLineMatch = line.match(/^(\s*)<Agent(?:\s[^>]*)?>([\s\S]*?)<\/Agent>\s*$/);
      if (singleLineMatch) {
        if (audience === "agent" && singleLineMatch[2]) {
          output.push(`${singleLineMatch[1]}${singleLineMatch[2]}`);
        }
        continue;
      }

      const openMatch = line.match(/^(\s*)<Agent(?:\s[^>]*)?>\s*$/);
      if (openMatch) {
        agentDepth += 1;
        continue;
      }

      const openWithContentMatch = line.match(/^(\s*)<Agent(?:\s[^>]*)?>(.*)$/);
      if (openWithContentMatch) {
        agentDepth += 1;
        if (audience === "agent" && openWithContentMatch[2]) {
          output.push(`${openWithContentMatch[1]}${openWithContentMatch[2]}`);
        }
        continue;
      }

      const closeWithContentMatch = line.match(/^(.*)<\/Agent>\s*$/);
      if (closeWithContentMatch && agentDepth > 0) {
        if (audience === "agent" && closeWithContentMatch[1]) {
          output.push(closeWithContentMatch[1]);
        }
        agentDepth = Math.max(0, agentDepth - 1);
        continue;
      }

      if (/^<\/Agent>\s*$/.test(trimmed) && agentDepth > 0) {
        agentDepth = Math.max(0, agentDepth - 1);
        continue;
      }
    }

    if (agentDepth > 0 && audience === "human") {
      continue;
    }

    output.push(line);
  }

  return output
    .join("\n")
    .replace(/\n{3,}/g, "\n\n")
    .trim();
}

function stripMdx(content: string): string {
  return content
    .replace(/^(import|export)\s.*$/gm, "")
    .replace(/<[^>]+\/>/g, "")
    .replace(/<\/?[A-Z][^>]*>/g, "")
    .replace(/<\/?[a-z][^>]*>/g, "")
    .replace(/\[([^\]]+)\]\([^)]+\)/g, "$1")
    .replace(/!\[([^\]]*)\]\([^)]+\)/g, "$1")
    .replace(/^#{1,6}\s+/gm, "")
    .replace(/(\*{1,3}|_{1,3})(.*?)\1/g, "$2")
    .replace(/```[\s\S]*?```/g, "")
    .replace(/`([^`]+)`/g, "$1")
    .replace(/^>\s+/gm, "")
    .replace(/^[-*_]{3,}\s*$/gm, "")
    .replace(/\n{3,}/g, "\n\n")
    .trim();
}

function resolveDocsSearchPageSource(dir: string): string | undefined {
  return ["page.mdx", "page.md"]
    .map((fileName) => path.join(dir, fileName))
    .find((candidate) => fs.existsSync(candidate));
}

function hasVisibleDescendantDocsSearchPage(dir: string): boolean {
  let entries: string[];
  try {
    entries = fs.readdirSync(dir);
  } catch {
    return false;
  }

  for (const name of entries.sort()) {
    const full = path.join(dir, name);
    try {
      if (!fs.statSync(full).isDirectory()) continue;
    } catch {
      continue;
    }

    const pageSource = resolveDocsSearchPageSource(full);
    if (pageSource) {
      try {
        const data = matter(fs.readFileSync(pageSource, "utf-8")).data;
        const hiddenFolderIndex = resolvePageSidebarFolderIndexBehavior(data.sidebar) === "hidden";
        if (data.hidden !== true && !hiddenFolderIndex) return true;
      } catch {
        return true;
      }
    }

    if (hasVisibleDescendantDocsSearchPage(full)) return true;
  }

  return false;
}

function isHiddenFolderIndexPageDir(dir: string): boolean {
  const pageSource = resolveDocsSearchPageSource(dir);
  if (!pageSource) return false;

  try {
    const data = matter(fs.readFileSync(pageSource, "utf-8")).data;
    return (
      resolvePageSidebarFolderIndexBehavior(data.sidebar) === "hidden" &&
      hasVisibleDescendantDocsSearchPage(dir)
    );
  } catch {
    return false;
  }
}

function scanDocsDir(
  docsDir: string,
  entry: string,
  locale?: string,
  excludedDirs: string[] = [],
  publicPath = `/${normalizePathSegment(entry) || "docs"}`,
): DocsSearchSourcePage[] {
  const indexes: Array<DocsSearchSourcePage & { relatedInput?: unknown }> = [];

  function isExcluded(dir: string) {
    const resolved = path.resolve(dir);
    return excludedDirs.some((excluded) => {
      const relative = path.relative(excluded, resolved);
      return relative === "" || (!relative.startsWith("..") && !path.isAbsolute(relative));
    });
  }

  function scan(dir: string, slugParts: string[]) {
    if (!fs.existsSync(dir)) return;
    if (isExcluded(dir)) return;

    const pageSource = resolveDocsSearchPageSource(dir);
    if (pageSource) {
      try {
        const raw = fs.readFileSync(pageSource, "utf-8");
        const { data } = matter(raw);
        const hiddenFolderIndex =
          resolvePageSidebarFolderIndexBehavior(data.sidebar) === "hidden" &&
          hasVisibleDescendantDocsSearchPage(dir);
        if (hiddenFolderIndex) {
          // Hidden folder indexes should redirect to their first child at the
          // app layer and should not remain searchable as standalone pages.
        } else {
          const title =
            (data.title as string) ||
            slugParts[slugParts.length - 1]?.replace(/-/g, " ") ||
            "Documentation";
          const description = data.description as string | undefined;
          const { content: fileContent } = matter(raw);
          const rawContent = resolveAgentMdxContent(fileContent, "human");
          const agentRawContent = resolveAgentMdxContent(fileContent, "agent");
          const content = stripMdx(rawContent);
          const baseUrl = publicDocsRoute(publicPath, slugParts);
          const url = withLangInUrl(baseUrl, locale);

          indexes.push({
            title,
            description,
            relatedInput: data.related,
            content,
            rawContent,
            agentFallbackRawContent: agentRawContent !== rawContent ? agentRawContent : undefined,
            url,
            sourcePath: pageSource.replace(/\\/g, "/"),
            lastModified: fs.statSync(pageSource).mtime.toISOString(),
            locale,
          });
        }
      } catch {
        // skip unreadable files
      }
    }

    let entries: string[];
    try {
      entries = fs.readdirSync(dir);
    } catch {
      return;
    }

    for (const name of entries.sort()) {
      const full = path.join(dir, name);
      try {
        if (fs.statSync(full).isDirectory()) {
          scan(full, [...slugParts, name]);
        }
      } catch {
        // skip
      }
    }
  }

  scan(docsDir, []);
  return resolveRelatedForSearchPages(indexes);
}

function scanChangelogDir(
  changelogDir: string,
  entryPath: string,
  changelogPath: string,
  locale?: string,
  publicPath = `/${normalizePathSegment(entryPath) || "docs"}`,
): DocsSearchSourcePage[] {
  if (!fs.existsSync(changelogDir)) return [];

  const indexes: Array<DocsSearchSourcePage & { relatedInput?: unknown }> = [];
  let entries: string[];

  try {
    entries = fs.readdirSync(changelogDir).sort().reverse();
  } catch {
    return indexes;
  }

  for (const name of entries) {
    if (!/^\d{4}-\d{2}-\d{2}$/.test(name)) continue;
    const entryDir = path.join(changelogDir, name);
    let isDirectory = false;

    try {
      isDirectory = fs.existsSync(entryDir) && fs.statSync(entryDir).isDirectory();
    } catch {
      continue;
    }

    if (!isDirectory) continue;

    const pagePath = path.join(entryDir, "page.mdx");
    if (!fs.existsSync(pagePath)) continue;

    try {
      const raw = fs.readFileSync(pagePath, "utf-8");
      const { data, content: fileContent } = matter(raw);
      if (data.draft === true) continue;

      const title = (data.title as string) || name.replace(/-/g, " ");
      const description = data.description as string | undefined;
      const rawContent = resolveAgentMdxContent(fileContent, "human");
      const agentRawContent = resolveAgentMdxContent(fileContent, "agent");
      const content = stripMdx(rawContent);
      const url = withLangInUrl(publicDocsRoute(publicPath, [changelogPath, name]), locale);
      const tags = Array.isArray(data.tags)
        ? data.tags.filter((value): value is string => typeof value === "string")
        : undefined;

      indexes.push({
        title,
        description,
        relatedInput: data.related,
        content,
        rawContent,
        agentFallbackRawContent: agentRawContent !== rawContent ? agentRawContent : undefined,
        url,
        locale,
        type: "changelog",
        version: typeof data.version === "string" ? data.version : undefined,
        tags,
      });
    } catch {
      // skip unreadable files
    }
  }

  return resolveRelatedForSearchPages(indexes);
}

function resolveRelatedForSearchPages(
  pages: Array<DocsSearchSourcePage & { relatedInput?: unknown }>,
): DocsSearchSourcePage[] {
  return pages.map(({ relatedInput, ...page }) => {
    const related = normalizeDocsRelated(relatedInput);
    return related.length > 0 ? { ...page, related } : page;
  });
}

function normalizePathSegment(value: string): string {
  return value.replace(/^\/+|\/+$/g, "");
}

function normalizeUrlPath(value: string): string {
  const normalized = value.replace(/\/+/g, "/");
  if (normalized === "/") return normalized;
  return normalized.replace(/\/+$/, "");
}

function normalizeRequestedMarkdownPath(entry: string, requestedPath: string): string {
  const trimmed = requestedPath.trim().replace(/\.md$/i, "");
  if (!trimmed) return `/${entry}`;

  const normalized = normalizeUrlPath(trimmed.startsWith("/") ? trimmed : `/${trimmed}`);
  const normalizedEntry = `/${normalizePathSegment(entry)}`;
  if (normalized === normalizedEntry || normalized.startsWith(`${normalizedEntry}/`)) {
    return normalized;
  }

  const slug = normalizePathSegment(trimmed);
  return slug ? normalizeUrlPath(`${normalizedEntry}/${slug}`) : normalizedEntry;
}

function normalizePublicRequestedMarkdownPath(ctx: DocsContext, requestedPath: string): string {
  const trimmed = requestedPath.trim().replace(/\.md$/i, "");
  if (!trimmed) return ctx.publicPath || "/";

  const normalized = normalizeUrlPath(trimmed.startsWith("/") ? trimmed : `/${trimmed}`);
  const normalizedEntry = `/${normalizePathSegment(ctx.entryPath)}`;
  if (normalized === normalizedEntry) return ctx.publicPath || "/";

  if (normalized.startsWith(`${normalizedEntry}/`)) {
    const suffix = normalized.slice(normalizedEntry.length + 1);
    return publicDocsRoute(ctx.publicPath, suffix.split("/").filter(Boolean));
  }

  if (ctx.publicPath) {
    if (normalized === ctx.publicPath || normalized.startsWith(`${ctx.publicPath}/`)) {
      return normalized;
    }
  } else if (normalized === "/") {
    return "/";
  }

  const slug = normalizePathSegment(trimmed);
  return publicDocsRoute(ctx.publicPath, slug ? slug.split("/").filter(Boolean) : []);
}

function normalizePublicDocsSlug(ctx: DocsContext, value: string): string {
  const pathname = normalizeUrlPath(value);
  const normalizedEntry = `/${normalizePathSegment(ctx.entryPath)}`;

  if (ctx.publicPath) {
    if (pathname === ctx.publicPath) return "";
    if (pathname.startsWith(`${ctx.publicPath}/`)) {
      return normalizePathSegment(pathname.slice(ctx.publicPath.length + 1));
    }
  } else if (pathname === "/") {
    return "";
  }

  if (pathname === normalizedEntry) return "";
  if (pathname.startsWith(`${normalizedEntry}/`)) {
    return normalizePathSegment(pathname.slice(normalizedEntry.length + 1));
  }

  return normalizePathSegment(pathname);
}

function getPublicMarkdownCanonicalLinkHeader({
  origin,
  ctx,
  requestedPath,
}: {
  origin: string;
  ctx: DocsContext;
  requestedPath: string;
}): string {
  const canonicalUrl = new URL(normalizePublicRequestedMarkdownPath(ctx, requestedPath), origin);
  if (ctx.locale) canonicalUrl.searchParams.set("lang", ctx.locale);
  return `<${canonicalUrl.toString()}>; rel="canonical"`;
}

function findDocsMcpPage(
  entry: string,
  pages: DocsMcpPage[],
  requestedPath: string,
): DocsMcpPage | null {
  const normalizedRequest = normalizeRequestedMarkdownPath(entry, requestedPath);

  for (const page of pages) {
    if (normalizeUrlPath(page.url) === normalizedRequest) return page;
  }

  const normalizedSlug = normalizePathSegment(
    requestedPath.replace(/^\//, "").replace(/\.md$/i, ""),
  );
  for (const page of pages) {
    if (normalizePathSegment(page.slug) === normalizedSlug) return page;
  }

  return null;
}

function acceptsMarkdown(request: Request): boolean {
  const accept = request.headers.get("accept");
  if (!accept) return false;
  return accept
    .split(",")
    .map((value) => value.trim())
    .some((value) => {
      const [mediaType, ...params] = value.split(";").map((part) => part.trim().toLowerCase());
      if (mediaType !== "text/markdown") return false;

      const qualityParam = params.find((param) => param.split("=", 1)[0]?.trim() === "q");
      if (!qualityParam) return true;

      const qualityValue = qualityParam.slice(qualityParam.indexOf("=") + 1).trim();
      const quality = Number.parseFloat(qualityValue);
      return Number.isFinite(quality) ? quality > 0 : true;
    });
}

type MarkdownRequest = {
  requestedPath: string;
  delivery: "api_format" | "md_route" | "accept_header" | "signature_agent";
};

type DocsContext = {
  entryPath: string;
  publicPath: string;
  docsDirs: string[];
  changelogDirs: string[];
  locale?: string;
};

function resolveMarkdownRequest(entry: string, url: URL, request: Request): MarkdownRequest | null {
  const format = url.searchParams.get("format")?.trim();
  if (format === "markdown") {
    return {
      requestedPath: url.searchParams.get("path")?.trim() ?? "",
      delivery: "api_format",
    };
  }

  const pathname = normalizeUrlPath(url.pathname);
  const normalizedEntry = `/${normalizePathSegment(entry)}`;

  if (pathname === `${normalizedEntry}.md`) {
    return { requestedPath: "", delivery: "md_route" };
  }

  const slugPrefix = `${normalizedEntry}/`;
  if (pathname.startsWith(slugPrefix) && pathname.endsWith(".md")) {
    return {
      requestedPath: pathname.slice(slugPrefix.length, -3),
      delivery: "md_route",
    };
  }

  const hasSignatureAgent = hasDocsMarkdownSignatureAgent(request);
  if (acceptsMarkdown(request) || hasSignatureAgent) {
    const delivery = hasSignatureAgent ? "signature_agent" : "accept_header";

    if (pathname === normalizedEntry) {
      return { requestedPath: "", delivery };
    }

    if (pathname.startsWith(slugPrefix)) {
      return {
        requestedPath: pathname.slice(slugPrefix.length),
        delivery,
      };
    }
  }

  return null;
}

function normalizeDocsPublicPath(value: string | undefined, entry: string): string {
  if (typeof value !== "string") return `/${normalizePathSegment(entry)}`;

  const cleaned = value.trim();
  if (cleaned === "" || cleaned === "/") return "";

  return `/${cleaned.replace(/^\/+|\/+$/g, "")}`;
}

function publicDocsRoute(publicPath: string, slugParts: string[] = []): string {
  const slug = slugParts.join("/");
  if (!slug) return publicPath || "/";
  return publicPath ? `${publicPath}/${slug}` : `/${slug}`;
}

function toPublicDocsUrl(value: string, entry: string, publicPath: string): string {
  const normalizedEntry = `/${normalizePathSegment(entry)}`;
  const hashIndex = value.indexOf("#");
  const hash = hashIndex >= 0 ? value.slice(hashIndex) : "";
  const withoutHash = hashIndex >= 0 ? value.slice(0, hashIndex) : value;
  const queryIndex = withoutHash.indexOf("?");
  const query = queryIndex >= 0 ? withoutHash.slice(queryIndex) : "";
  const pathname = normalizeUrlPath(
    queryIndex >= 0 ? withoutHash.slice(0, queryIndex) : withoutHash,
  );

  if (publicPath === normalizedEntry) return value;
  if (pathname === normalizedEntry) return `${publicPath || "/"}${query}${hash}`;
  if (pathname.startsWith(`${normalizedEntry}/`)) {
    const suffix = pathname.slice(normalizedEntry.length + 1);
    const publicPathname = publicPath ? `${publicPath}/${suffix}` : `/${suffix}`;
    return `${publicPathname}${query}${hash}`;
  }

  return value;
}

function withPublicDocsUrl<T extends { url: string }>(page: T, ctx: DocsContext): T {
  const url = toPublicDocsUrl(page.url, ctx.entryPath, ctx.publicPath);
  return url === page.url ? page : { ...page, url };
}

function readDocsPath(root: string): string | undefined {
  for (const ext of FILE_EXTS) {
    const configPath = path.join(root, `docs.config.${ext}`);
    if (!fs.existsSync(configPath)) continue;

    try {
      const content = fs.readFileSync(configPath, "utf-8");
      const match = content.match(/docsPath\s*:\s*["']([^"']*)["']/);
      if (match) return match[1];
    } catch {
      // fall through
    }
  }

  return undefined;
}

function resolvePublicMarkdownRequest(
  entry: string,
  docsPath: string,
  url: URL,
  request: Request,
): MarkdownRequest | null {
  if (docsPath === `/${normalizePathSegment(entry)}`) return null;

  const pathname = normalizeUrlPath(url.pathname);

  if (docsPath === "") {
    if (pathname === `/${normalizePathSegment(entry)}.md`) {
      return { requestedPath: "", delivery: "md_route" };
    }

    if (pathname !== "/docs.md" && pathname.endsWith(".md")) {
      return {
        requestedPath: pathname.slice(1, -3),
        delivery: "md_route",
      };
    }

    const hasSignatureAgent = hasDocsMarkdownSignatureAgent(request);
    if (acceptsMarkdown(request) || hasSignatureAgent) {
      const delivery = hasSignatureAgent ? "signature_agent" : "accept_header";
      return {
        requestedPath: pathname === "/" ? "" : pathname.slice(1),
        delivery,
      };
    }

    return null;
  }

  if (pathname === `${docsPath}.md`) {
    return { requestedPath: "", delivery: "md_route" };
  }

  const slugPrefix = `${docsPath}/`;
  if (pathname.startsWith(slugPrefix) && pathname.endsWith(".md")) {
    return {
      requestedPath: pathname.slice(slugPrefix.length, -3),
      delivery: "md_route",
    };
  }

  const hasSignatureAgent = hasDocsMarkdownSignatureAgent(request);
  if (acceptsMarkdown(request) || hasSignatureAgent) {
    const delivery = hasSignatureAgent ? "signature_agent" : "accept_header";

    if (pathname === docsPath) {
      return { requestedPath: "", delivery };
    }

    if (pathname.startsWith(slugPrefix)) {
      return {
        requestedPath: pathname.slice(slugPrefix.length),
        delivery,
      };
    }
  }

  return null;
}

