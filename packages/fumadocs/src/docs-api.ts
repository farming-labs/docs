interface ChatMessage {
  role: "user" | "assistant" | "system";
  content: string;
}

function resolveModelAndProvider(
  aiConfig: AIOptions,
  requestedModelId?: string,
): { model: string; baseUrl: string; apiKey: string | undefined } {
  const raw = aiConfig.model as AIModelConfig | string | undefined;

  // Find the model list (from nested or flat config)
  const modelList: AIModelEntry[] = (typeof raw === "object" && raw?.models) || [];

  // Resolve model id
  let modelId = requestedModelId;
  if (!modelId) {
    if (typeof raw === "string") modelId = raw;
    else if (typeof raw === "object") modelId = raw.defaultModel ?? raw.models?.[0]?.id;
    if (!modelId) modelId = "gpt-4o-mini";
  }

  // Find matching model entry to get its provider key
  const entry = modelList.find((m) => m.id === modelId);
  const providerKey = entry?.provider;

  // Resolve provider config
  const providerConfig = providerKey && aiConfig.providers?.[providerKey];

  const baseUrl = (
    (providerConfig && providerConfig.baseUrl) ||
    aiConfig.baseUrl ||
    "https://api.openai.com/v1"
  ).replace(/\/$/, "");

  const apiKey =
    (providerConfig && providerConfig.apiKey) || aiConfig.apiKey || process.env.OPENAI_API_KEY;

  return { model: modelId, baseUrl, apiKey };
}

function safeUrlOrigin(value: string): string {
  try {
    return new URL(value).origin;
  } catch {
    return value;
  }
}

async function handleAskAI(
  request: Request,
  indexes: DocsSearchSourcePage[],
  aiConfig: AIOptions,
  search: boolean | DocsSearchConfig | undefined,
  analytics?: boolean | DocsAnalyticsConfig,
  observability?: boolean | DocsObservabilityConfig,
  analyticsContext: { locale?: string } = {},
): Promise<Response> {
  const url = new URL(request.url);
  const requestStartedAt = Date.now();
  const trace = createDocsAgentTraceContext("ask-ai");
  const runSpanId = createDocsAgentTraceId("span");
  const traceBase = {
    source: "server" as const,
    traceId: trace.traceId,
    url: request.url,
    path: url.pathname,
    locale: analyticsContext.locale,
  };

  async function emitTrace(event: DocsAgentTraceEventInput): Promise<void> {
    await emitDocsAgentTraceEvent(observability, {
      ...traceBase,
      ...event,
    });
  }

  async function emitRunError(
    reason: string,
    outputPreview: Record<string, unknown> = {},
  ): Promise<void> {
    const endedAt = new Date().toISOString();
    const elapsed = Math.max(0, Date.now() - requestStartedAt);
    const common = {
      name: "ask-ai",
      startedAt: trace.startedAt,
      endedAt,
      durationMs: elapsed,
      status: "error" as const,
      outputPreview: {
        reason,
        ...outputPreview,
      },
      metadata: { reason },
    };

    await emitTrace({
      ...common,
      type: "error",
      parentSpanId: runSpanId,
    });
    await emitTrace({
      ...common,
      type: "run.error",
      spanId: runSpanId,
    });
    await emitTrace({
      ...common,
      type: "run.end",
      spanId: runSpanId,
    });
  }

  await emitTrace({
    type: "run.start",
    name: "ask-ai",
    spanId: runSpanId,
    startedAt: trace.startedAt,
    durationMs: 0,
    status: "started",
    inputPreview: {
      method: request.method,
      path: url.pathname,
    },
  });

  // ── Parse request ──────────────────────────────────────────────
  let body: { messages?: ChatMessage[]; model?: string };
  try {
    body = await request.json();
  } catch {
    await emitDocsAnalyticsEvent(analytics, {
      type: "api_ai_error",
      source: "server",
      url: request.url,
      path: url.pathname,
      locale: analyticsContext.locale,
      properties: {
        reason: "invalid_json",
        durationMs: Math.max(0, Date.now() - requestStartedAt),
      },
    });
    await emitRunError("invalid_json", { status: 400 });
    return Response.json(
      { error: "Invalid JSON body. Expected { messages: [...] }" },
      { status: 400 },
    );
  }

  const messages = body.messages;
  if (!Array.isArray(messages) || messages.length === 0) {
    await emitDocsAnalyticsEvent(analytics, {
      type: "api_ai_error",
      source: "server",
      url: request.url,
      path: url.pathname,
      locale: analyticsContext.locale,
      properties: {
        reason: "missing_messages",
        durationMs: Math.max(0, Date.now() - requestStartedAt),
      },
    });
    await emitRunError("missing_messages", { status: 400 });
    return Response.json(
      { error: "messages array is required and must not be empty." },
      { status: 400 },
    );
  }

  const lastUserMessage = [...messages].reverse().find((m) => m.role === "user");
  if (!lastUserMessage) {
    await emitDocsAnalyticsEvent(analytics, {
      type: "api_ai_error",
      source: "server",
      url: request.url,
      path: url.pathname,
      locale: analyticsContext.locale,
      properties: {
        reason: "missing_user_message",
        messageCount: messages.length,
        durationMs: Math.max(0, Date.now() - requestStartedAt),
      },
    });
    await emitRunError("missing_user_message", { status: 400, messageCount: messages.length });
    return Response.json({ error: "At least one user message is required." }, { status: 400 });
  }

  // ── Search for relevant docs (RAG retrieval) ───────────────────
  const maxResults = aiConfig.maxResults ?? 5;
  const query = lastUserMessage.content;
  await emitTrace({
    type: "user.input",
    name: "ask-ai",
    parentSpanId: runSpanId,
    startedAt: new Date().toISOString(),
    endedAt: new Date().toISOString(),
    durationMs: 0,
    status: "success",
    inputPreview: {
      messageCount: messages.length,
      questionLength: query.length,
      requestedModel:
        typeof body.model === "string" && body.model.trim().length > 0
          ? body.model.trim()
          : undefined,
    },
  });

  const retrievalStartedAt = Date.now();
  const retrievalStartedAtIso = new Date().toISOString();
  const retrievalSpanId = createDocsAgentTraceId("span");
  await emitTrace({
    type: "retrieval.query",
    name: "docs-index",
    spanId: retrievalSpanId,
    parentSpanId: runSpanId,
    startedAt: retrievalStartedAtIso,
    status: "started",
    inputPreview: {
      queryLength: query.length,
      maxResults,
      indexSize: indexes.length,
    },
  });
  const retrieval = await buildDocsAskAIContext({
    pages: indexes,
    query,
    search,
    locale: analyticsContext.locale,
    pathname: url.searchParams.get("pathname") ?? undefined,
    siteTitle: "Documentation",
    baseUrl: url.origin,
    limit: maxResults,
  });
  const scored = retrieval.results;
  await emitTrace({
    type: "retrieval.result",
    name: "docs-index",
    parentSpanId: retrievalSpanId,
    startedAt: retrievalStartedAtIso,
    endedAt: new Date().toISOString(),
    durationMs: Math.max(0, Date.now() - retrievalStartedAt),
    status: "success",
    outputPreview: {
      resultCount: scored.length,
      urls: scored.slice(0, 5).map((doc) => doc.url),
    },
    metadata: {
      maxResults,
      indexSize: indexes.length,
    },
  });

  // Build context from relevant docs
  const promptStartedAt = Date.now();
  const promptStartedAtIso = new Date().toISOString();
  const promptSpanId = createDocsAgentTraceId("span");
  const context = retrieval.context;

  // ── Build LLM messages ─────────────────────────────────────────
  const systemPrompt = aiConfig.systemPrompt ?? buildDefaultSystemPrompt(aiConfig);
  const packageHintsPrompt = formatDocsAskAIPackageHints(
    retrieval.packageHints,
    aiConfig.packageName,
  );
  const fullSystemPrompt = [systemPrompt, packageHintsPrompt].filter(Boolean).join("\n\n");
  const systemMessage: ChatMessage = {
    role: "system",
    content: context
      ? `${fullSystemPrompt}\n\n---\n\nDocumentation context:\n\n${context}`
      : fullSystemPrompt,
  };

  const llmMessages: ChatMessage[] = [
    systemMessage,
    ...messages.filter((m) => m.role !== "system"),
  ];
  await emitTrace({
    type: "prompt.build",
    name: "ask-ai.prompt",
    spanId: promptSpanId,
    parentSpanId: runSpanId,
    startedAt: promptStartedAtIso,
    endedAt: new Date().toISOString(),
    durationMs: Math.max(0, Date.now() - promptStartedAt),
    status: "success",
    inputPreview: {
      messageCount: messages.length,
      retrievedCount: scored.length,
    },
    outputPreview: {
      llmMessageCount: llmMessages.length,
      contextChars: context.length,
      systemMessageChars: systemMessage.content.length,
    },
  });

  // ── Resolve model + provider ────────────────────────────────────
  const requestedModel =
    typeof body.model === "string" && body.model.trim().length > 0 ? body.model.trim() : undefined;

  const resolved = resolveModelAndProvider(aiConfig, requestedModel);

  if (!resolved.apiKey) {
    await emitDocsAnalyticsEvent(analytics, {
      type: "api_ai_error",
      source: "server",
      url: request.url,
      path: url.pathname,
      locale: analyticsContext.locale,
      input: { question: query },
      properties: {
        reason: "missing_api_key",
        messageCount: messages.length,
        questionLength: query.length,
        retrievedCount: scored.length,
        model: resolved.model,
        durationMs: Math.max(0, Date.now() - requestStartedAt),
      },
    });
    await emitRunError("missing_api_key", {
      status: 500,
      messageCount: messages.length,
      questionLength: query.length,
      retrievedCount: scored.length,
      model: resolved.model,
    });
    return Response.json(
      {
        error: `AI is enabled but no API key was found. Either set apiKey in your docs.config ai section, configure a provider, or add OPENAI_API_KEY to your .env.local file.`,
      },
      { status: 500 },
    );
  }

  await emitDocsAnalyticsEvent(analytics, {
    type: "api_ai_request",
    source: "server",
    url: request.url,
    path: url.pathname,
    locale: analyticsContext.locale,
    input: { question: query },
    properties: {
      messageCount: messages.length,
      questionLength: query.length,
      retrievedCount: scored.length,
      model: resolved.model,
    },
  });

  const modelStartedAt = Date.now();
  const modelStartedAtIso = new Date().toISOString();
  const modelSpanId = createDocsAgentTraceId("span");
  const providerOrigin = safeUrlOrigin(resolved.baseUrl);
  await emitTrace({
    type: "model.call",
    name: resolved.model,
    spanId: modelSpanId,
    parentSpanId: runSpanId,
    startedAt: modelStartedAtIso,
    status: "started",
    inputPreview: {
      messageCount: llmMessages.length,
      stream: true,
      providerOrigin,
    },
    metadata: {
      model: resolved.model,
    },
  });

  let llmResponse: Response;
  try {
    llmResponse = await fetch(`${resolved.baseUrl}/chat/completions`, {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        Authorization: `Bearer ${resolved.apiKey}`,
      },
      body: JSON.stringify({
        model: resolved.model,
        stream: true,
        messages: llmMessages,
      }),
    });
  } catch (error) {
    const elapsed = Math.max(0, Date.now() - modelStartedAt);
    const message = error instanceof Error ? error.message : "Unknown error";
    await emitTrace({
      type: "model.error",
      name: resolved.model,
      parentSpanId: modelSpanId,
      startedAt: modelStartedAtIso,
      endedAt: new Date().toISOString(),
      durationMs: elapsed,
      status: "error",
      outputPreview: {
        message,
      },
      metadata: {
        model: resolved.model,
        providerOrigin,
      },
    });
    await emitDocsAnalyticsEvent(analytics, {
      type: "api_ai_error",
      source: "server",
      url: request.url,
      path: url.pathname,
      locale: analyticsContext.locale,
      input: { question: query },
      properties: {
        reason: "llm_fetch_error",
        messageCount: messages.length,
        questionLength: query.length,
        retrievedCount: scored.length,
        model: resolved.model,
        durationMs: Math.max(0, Date.now() - requestStartedAt),
      },
    });
    await emitRunError("llm_fetch_error", {
      status: 502,
      messageCount: messages.length,
      questionLength: query.length,
      retrievedCount: scored.length,
      model: resolved.model,
    });
    return Response.json({ error: "LLM API request failed." }, { status: 502 });
  }

  if (!llmResponse.ok) {
    const errText = await llmResponse.text().catch(() => "Unknown error");
    const elapsed = Math.max(0, Date.now() - modelStartedAt);
    await emitTrace({
      type: "model.error",
      name: resolved.model,
      parentSpanId: modelSpanId,
      startedAt: modelStartedAtIso,
      endedAt: new Date().toISOString(),
      durationMs: elapsed,
      status: "error",
      outputPreview: {
        status: llmResponse.status,
        errorChars: errText.length,
      },
      metadata: {
        model: resolved.model,
        providerOrigin,
      },
    });
    await emitDocsAnalyticsEvent(analytics, {
      type: "api_ai_error",
      source: "server",
      url: request.url,
      path: url.pathname,
      locale: analyticsContext.locale,
      input: { question: query },
      properties: {
        reason: "llm_error",
        status: llmResponse.status,
        messageCount: messages.length,
        questionLength: query.length,
        retrievedCount: scored.length,
        model: resolved.model,
        durationMs: Math.max(0, Date.now() - requestStartedAt),
      },
    });
    await emitRunError("llm_error", {
      status: 502,
      modelStatus: llmResponse.status,
      messageCount: messages.length,
      questionLength: query.length,
      retrievedCount: scored.length,
      model: resolved.model,
    });
    return Response.json(
      { error: `LLM API error (${llmResponse.status}): ${errText}` },
      { status: 502 },
    );
  }

  await emitDocsAnalyticsEvent(analytics, {
    type: "api_ai_response",
    source: "server",
    url: request.url,
    path: url.pathname,
    locale: analyticsContext.locale,
    input: { question: query },
    properties: {
      messageCount: messages.length,
      questionLength: query.length,
      retrievedCount: scored.length,
      model: resolved.model,
      durationMs: Math.max(0, Date.now() - requestStartedAt),
    },
  });
  const responseEndedAt = new Date().toISOString();
  const modelDurationMs = Math.max(0, Date.now() - modelStartedAt);
  await emitTrace({
    type: "model.response",
    name: resolved.model,
    parentSpanId: modelSpanId,
    startedAt: modelStartedAtIso,
    endedAt: responseEndedAt,
    durationMs: modelDurationMs,
    status: "success",
    outputPreview: {
      status: llmResponse.status,
      stream: true,
      contentType: llmResponse.headers.get("content-type") ?? undefined,
    },
    metadata: {
      model: resolved.model,
      providerOrigin,
    },
  });
  await emitTrace({
    type: "model.stream",
    name: resolved.model,
    parentSpanId: modelSpanId,
    startedAt: modelStartedAtIso,
    endedAt: responseEndedAt,
    durationMs: modelDurationMs,
    status: "success",
    outputPreview: {
      stream: true,
    },
    metadata: {
      model: resolved.model,
    },
  });

  const runDurationMs = Math.max(0, Date.now() - requestStartedAt);
  await emitTrace({
    type: "agent.final",
    name: "ask-ai",
    parentSpanId: runSpanId,
    startedAt: trace.startedAt,
    endedAt: new Date().toISOString(),
    durationMs: runDurationMs,
    status: "success",
    outputPreview: {
      stream: true,
      retrievedCount: scored.length,
    },
    metadata: {
      model: resolved.model,
    },
  });
  await emitTrace({
    type: "run.end",
    name: "ask-ai",
    spanId: runSpanId,
    startedAt: trace.startedAt,
    endedAt: new Date().toISOString(),
    durationMs: runDurationMs,
    status: "success",
    outputPreview: {
      stream: true,
      retrievedCount: scored.length,
    },
    metadata: {
      model: resolved.model,
    },
  });

  // Proxy the SSE stream directly to the client
  return new Response(llmResponse.body, {
    headers: {
      "Content-Type": "text/event-stream",
      "Cache-Control": "no-cache",
      Connection: "keep-alive",
    },
  });
}

// ─── llms.txt generation ────────────────────────────────────────────

type LlmsTxtOptions = LlmsTxtConfig;

function readLlmsTxtConfig(root: string): LlmsTxtOptions & { enabled: boolean } {
  for (const ext of FILE_EXTS) {
    const configPath = path.join(root, `docs.config.${ext}`);
    if (fs.existsSync(configPath)) {
      try {
        const content = fs.readFileSync(configPath, "utf-8");
        const navTitleMatch = content.match(/nav\s*:\s*\{[^}]*title\s*:\s*["']([^"']+)["']/s);

        if (!content.includes("llmsTxt")) {
          return {
            enabled: true,
            siteTitle: navTitleMatch?.[1],
          };
        }

        const isBoolTrue = /llmsTxt\s*:\s*true/.test(content);
        if (isBoolTrue) {
          return {
            enabled: true,
            siteTitle: navTitleMatch?.[1],
          };
        }

        const isBoolFalse = /llmsTxt\s*:\s*false/.test(content);
        if (isBoolFalse) return { enabled: false };

        const enabledMatch = content.match(/llmsTxt\s*:\s*\{[^}]*enabled\s*:\s*(true|false)/s);
        if (enabledMatch && enabledMatch[1] === "false") return { enabled: false };

        const baseUrlMatch = content.match(/llmsTxt\s*:\s*\{[^}]*baseUrl\s*:\s*["']([^"']+)["']/s);
        const siteTitleMatch = content.match(
          /llmsTxt\s*:\s*\{[^}]*siteTitle\s*:\s*["']([^"']+)["']/s,
        );
        const siteDescMatch = content.match(
          /llmsTxt\s*:\s*\{[^}]*siteDescription\s*:\s*["']([^"']+)["']/s,
        );

        return {
          enabled: true,
          baseUrl: baseUrlMatch?.[1],
          siteTitle: siteTitleMatch?.[1] ?? navTitleMatch?.[1],
          siteDescription: siteDescMatch?.[1],
        };
      } catch {
        // fall through
      }
    }
  }
  return { enabled: true };
}

function resolveLlmsTxtConfig(
  input: boolean | LlmsTxtOptions | undefined,
  fallback: LlmsTxtOptions & { enabled: boolean },
): LlmsTxtOptions & { enabled: boolean } {
  if (input === undefined) return fallback;
  if (typeof input === "boolean") {
    return input ? { ...fallback, enabled: true } : { enabled: false };
  }

  return {
    ...fallback,
    ...input,
    enabled: input.enabled ?? true,
  };
}

function readSitemapConfig(root: string): boolean | DocsSitemapConfig | undefined {
  for (const ext of FILE_EXTS) {
    const configPath = path.join(root, `docs.config.${ext}`);
    if (!fs.existsSync(configPath)) continue;

    try {
      const content = fs.readFileSync(configPath, "utf-8");
      if (!content.includes("sitemap")) return undefined;
      if (/sitemap\s*:\s*false/.test(content)) return false;
      if (/sitemap\s*:\s*true/.test(content)) return true;

      const sitemapBlock = content.match(/sitemap\s*:\s*\{([\s\S]*?)\n\s*\}/)?.[1] ?? "";
      const routePrefix = sitemapBlock.match(/routePrefix\s*:\s*["']([^"']+)["']/)?.[1];
      const baseUrl = sitemapBlock.match(/baseUrl\s*:\s*["']([^"']+)["']/)?.[1];
      const manifestPath = sitemapBlock.match(/manifestPath\s*:\s*["']([^"']+)["']/)?.[1];
      const enabledMatch = sitemapBlock.match(/enabled\s*:\s*(true|false)/);

      return {
        enabled: enabledMatch ? enabledMatch[1] === "true" : true,
        routePrefix,
        baseUrl,
        manifestPath,
      };
    } catch {
      return undefined;
    }
  }

  return undefined;
}

function readRobotsConfig(root: string): boolean | DocsRobotsConfig | undefined {
  for (const ext of FILE_EXTS) {
    const configPath = path.join(root, `docs.config.${ext}`);
    if (!fs.existsSync(configPath)) continue;

    try {
      const content = fs.readFileSync(configPath, "utf-8");
      if (!content.includes("robots")) return undefined;
      if (/robots\s*:\s*false/.test(content)) return false;
      if (/robots\s*:\s*true/.test(content)) return true;

      const robotsBlock = content.match(/robots\s*:\s*\{([\s\S]*?)\n\s*\}/)?.[1] ?? "";
      const enabledMatch = robotsBlock.match(/enabled\s*:\s*(true|false)/);
      const pathMatch = robotsBlock.match(/path\s*:\s*["']([^"']+)["']/);
      const baseUrlMatch = robotsBlock.match(/baseUrl\s*:\s*["']([^"']+)["']/);
      const aiMatch = robotsBlock.match(/ai\s*:\s*["'](allow|disallow)["']/);

      return {
        enabled: enabledMatch ? enabledMatch[1] === "true" : true,
        path: pathMatch?.[1],
        baseUrl: baseUrlMatch?.[1],
        ai: aiMatch?.[1] as DocsRobotsConfig["ai"],
      };
    } catch {
      return undefined;
    }
  }

  return undefined;
}

function readApiReferenceConfig(root: string): DocsConfig["apiReference"] | undefined {
  for (const ext of FILE_EXTS) {
    const configPath = path.join(root, `docs.config.${ext}`);
    if (!fs.existsSync(configPath)) continue;

    try {
      const content = fs.readFileSync(configPath, "utf-8");
      if (!content.includes("apiReference")) return undefined;
      if (/apiReference\s*:\s*false/.test(content)) return false;
      if (/apiReference\s*:\s*true/.test(content)) return true;

      const block = content.match(/apiReference\s*:\s*\{([\s\S]*?)\n\s*\}/)?.[1] ?? "";
      const enabledMatch = block.match(/enabled\s*:\s*(true|false)/);
      const pathMatch = block.match(/path\s*:\s*["']([^"']+)["']/);
      const routeRootMatch = block.match(/routeRoot\s*:\s*["']([^"']+)["']/);
      const specUrlMatch = block.match(/specUrl\s*:\s*["']([^"']+)["']/);
      const rendererMatch = block.match(/renderer\s*:\s*["'](fumadocs|scalar)["']/);
      const excludeMatch = block.match(/exclude\s*:\s*\[([\s\S]*?)\]/);
      const exclude =
        excludeMatch?.[1] === undefined
          ? undefined
          : Array.from(excludeMatch[1].matchAll(/["']([^"']+)["']/g))
              .map((match) => match[1])
              .filter(Boolean);
      const renderer =
        rendererMatch?.[1] === "fumadocs" || rendererMatch?.[1] === "scalar"
          ? rendererMatch[1]
          : undefined;

      return {
        enabled: enabledMatch ? enabledMatch[1] === "true" : true,
        path: pathMatch?.[1],
        routeRoot: routeRootMatch?.[1],
        specUrl: specUrlMatch?.[1],
        renderer,
        exclude,
      };
    } catch {
      return undefined;
    }
  }

  return undefined;
}

function generateLlmsTxt(
  indexes: DocsSearchSourcePage[],
  options: LlmsTxtOptions,
): ReturnType<typeof renderDocsLlmsTxt> {
  return renderDocsLlmsTxt(indexes, options);
}

// ─── createDocsAPI ──────────────────────────────────────────────────

/**
 * Create a unified docs API route handler.
 *
 * Returns `{ GET, POST }` for use in a Next.js route handler:
 * - **GET  ?query=…**        → full-text search
 * - **GET  ?format=llms**     → llms.txt (concise page listing)
 * - **GET  ?format=llms-full** → llms-full.txt (full page content)
 * - **GET  ?format=skill**    → skill.md
 * - **POST**                  → AI-powered chat with RAG
 *
 * @example
 * ```ts
 * // app/api/docs/route.ts (auto-generated by withDocs)
 * import { createDocsAPI } from "@farming-labs/next/api";
 * export const { GET, POST } = createDocsAPI();
 * export const revalidate = false;
 * ```
 *
 * @param options - Optional overrides (entry, language, ai config)
 */
/**
 * @deprecated Prefer `createDocsAPI` from `@farming-labs/next/api` in Next.js apps.
 * The `@farming-labs/theme/api` path is kept for compatibility and will be phased out.
 */
export function createDocsAPI(options?: DocsAPIOptions) {
  const root = options?.rootDir ?? process.cwd();
  const entry = options?.entry ?? readEntry(root);
  const docsPath = normalizeDocsPublicPath(options?.docsPath ?? readDocsPath(root), entry);
  const analytics = options?.analytics;
  const observability = options?.observability;
  const appDir = getNextAppDir(root);
  const contentDir = options?.contentDir ?? path.join(appDir, entry);
  const changelogConfig = resolveChangelogConfig(options?.changelog);
  const agentFeedbackConfig = resolveAgentFeedbackConfig(options?.feedback);

  const i18nConfig = options?.i18n ?? readI18nConfig(root);
  const i18n = resolveDocsI18n(i18nConfig);

  // Read AI config from docs.config if not explicitly provided
  const aiConfig: AIOptions = options?.ai ?? readAIConfig(root);
  const searchConfig = options?.search;

  // Read llms.txt config
  const llmsConfig = resolveLlmsTxtConfig(options?.llmsTxt, readLlmsTxtConfig(root));
  const sitemapConfig = options?.sitemap ?? readSitemapConfig(root);
  const robotsConfig = options?.robots ?? readRobotsConfig(root);
  const apiReferenceConfig = options?.apiReference ?? readApiReferenceConfig(root);
  const devToolsEnabled = resolveDevToolsEnabled(options?.devTools);
  const apiReferenceDocsConfig = {
    ...options,
    entry,
    apiReference: apiReferenceConfig,
  } as DocsConfig;
  const openapiDiscovery = resolveApiReferenceOpenApiDiscovery(apiReferenceConfig);
  const mcpConfig = resolveDocsMcpConfig(options?.mcp ?? readMcpConfig(root), {
    defaultName: llmsConfig.siteTitle ?? "Documentation",
  });

  function resolveDocsDirCandidates(locale?: string): string[] {
    const relativeCandidates = new Set<string>();

    if (path.isAbsolute(contentDir)) {
      return [locale ? path.join(contentDir, locale) : contentDir];
    }

    relativeCandidates.add(contentDir);
    relativeCandidates.add(path.join("app", entry));
    relativeCandidates.add(path.join("src", "app", entry));

    const rootCandidates = new Set<string>([root]);
    rootCandidates.add(process.cwd());
    if (path.basename(root) === "server") {
      rootCandidates.add(path.resolve(root, "..", ".."));
    } else {
      rootCandidates.add(path.join(root, ".next", "server"));
      rootCandidates.add(path.join(root, ".next-build", "server"));
    }

    const resolved = new Set<string>();
    for (const base of rootCandidates) {
      for (const relative of relativeCandidates) {
        const candidate = path.join(base, relative);
        resolved.add(locale ? path.join(candidate, locale) : candidate);
      }
    }

    return [...resolved];
  }

  function resolveLocaleFromRequest(request: Request): string | undefined {
    if (!i18n) return undefined;
    const url = new URL(request.url);
    const direct = resolveDocsLocale(url.searchParams, i18n);
    if (direct) return direct;

    const referrer = request.headers.get("referer") ?? request.headers.get("referrer");
    if (referrer) {
      try {
        const refUrl = new URL(referrer);
        const fromRef = resolveDocsLocale(refUrl.searchParams, i18n);
        if (fromRef) return fromRef;
      } catch {
        // ignore
      }
    }

    return i18n.defaultLocale;
  }

  function resolveChangelogDirCandidates(docsDirs: string[]): string[] {
    if (!changelogConfig.enabled) return [];

    if (path.isAbsolute(changelogConfig.contentDir)) {
      return [changelogConfig.contentDir];
    }

    return docsDirs.map((docsDir) => path.join(docsDir, changelogConfig.contentDir));
  }

  function isWithinDir(candidate: string, target: string) {
    const relative = path.relative(target, candidate);
    return relative === "" || (!relative.startsWith("..") && !path.isAbsolute(relative));
  }

  function resolveContextFromRequest(request: Request): DocsContext {
    if (!i18n) {
      const docsDirs = resolveDocsDirCandidates();
      return {
        entryPath: entry,
        publicPath: docsPath,
        docsDirs,
        changelogDirs: resolveChangelogDirCandidates(docsDirs),
      };
    }

    const locale = resolveLocaleFromRequest(request) ?? i18n.defaultLocale;
    const docsDirs = resolveDocsDirCandidates(locale);
    return {
      entryPath: entry,
      publicPath: docsPath,
      locale,
      docsDirs,
      changelogDirs: resolveChangelogDirCandidates(docsDirs),
    };
  }

  const indexesByLocale = new Map<string, DocsSearchSourcePage[]>();
  const llmsCacheByLocale = new Map<string, ReturnType<typeof renderDocsLlmsTxt>>();
  const markdownSourcesByLocale = new Map<
    string,
    ReturnType<typeof createFilesystemDocsMcpSource>[]
  >();

  function getIndexes(ctx: DocsContext) {
    const key = ctx.locale ?? "__default__";
    const cached = indexesByLocale.get(key);
    if (cached) return cached;

    let next: DocsSearchSourcePage[] = [];
    for (const docsDir of ctx.docsDirs) {
      const excludedDirs = ctx.changelogDirs.filter((dir) => isWithinDir(dir, docsDir));
      const docsPages = scanDocsDir(
        docsDir,
        ctx.entryPath,
        ctx.locale,
        excludedDirs,
        ctx.publicPath,
      );
      if (docsPages.length === 0) continue;

      next = docsPages;
      break;
    }

    if (changelogConfig.enabled) {
      const changelogPages = ctx.changelogDirs.flatMap((dir) =>
        scanChangelogDir(dir, ctx.entryPath, changelogConfig.path, ctx.locale, ctx.publicPath),
      );
      next = [...next, ...changelogPages];
    }

    indexesByLocale.set(key, next);
    return next;
  }

  function getMarkdownSources(ctx: DocsContext) {
    const key = ctx.locale ?? "__default__";
    const cached = markdownSourcesByLocale.get(key);
    if (cached) return cached;

    const sources = ctx.docsDirs.map((docsDir) =>
      createFilesystemDocsMcpSource({
        rootDir: root,
        entry: ctx.entryPath,
        contentDir: docsDir,
      }),
    );
    markdownSourcesByLocale.set(key, sources);
    return sources;
  }

  async function getMarkdownDocument(ctx: DocsContext, requestedPath: string) {
    const normalizedRequest = normalizeRequestedMarkdownPath(ctx.entryPath, requestedPath);
    const normalizedPublicRequest = normalizePublicRequestedMarkdownPath(ctx, requestedPath);
    const normalizedEntry = `/${normalizePathSegment(ctx.entryPath)}`;
    const relativeSlug =
      normalizedRequest === normalizedEntry
        ? ""
        : normalizedRequest.slice(normalizedEntry.length).replace(/^\/+/, "");

    for (const docsDir of ctx.docsDirs) {
      const candidateDir = relativeSlug ? path.join(docsDir, ...relativeSlug.split("/")) : docsDir;
      if (isHiddenFolderIndexPageDir(candidateDir)) return null;
    }

    for (const source of getMarkdownSources(ctx)) {
      const page = findDocsMcpPage(ctx.entryPath, await source.getPages(), requestedPath);
      if (page)
        return renderMarkdownDocument(withPublicDocsUrl(page, ctx), {
          llmsEnabled: llmsConfig.enabled,
        });
    }

    const fallbackPage = getIndexes(ctx).find((page) => {
      const pageUrl = normalizeUrlPath(page.url);
      return pageUrl === normalizedRequest || pageUrl === normalizedPublicRequest;
    });
    if (fallbackPage)
      return renderMarkdownDocument(withPublicDocsUrl(fallbackPage, ctx), {
        llmsEnabled: llmsConfig.enabled,
      });

    const requestedSlug = normalizePublicDocsSlug(ctx, normalizedPublicRequest);
    for (const page of getIndexes(ctx)) {
      const slug = normalizePublicDocsSlug(ctx, page.url);
      if (slug === requestedSlug)
        return renderMarkdownDocument(withPublicDocsUrl(page, ctx), {
          llmsEnabled: llmsConfig.enabled,
        });
    }

    return null;
  }

  function getLlmsContent(ctx: DocsContext) {
    const key = ctx.locale ?? "__default__";
    const cached = llmsCacheByLocale.get(key);
    if (cached) return cached;
    const next = generateLlmsTxt(getIndexes(ctx), {
      siteTitle: llmsConfig.siteTitle ?? "Documentation",
      siteDescription: llmsConfig.siteDescription,
      baseUrl: llmsConfig.baseUrl ?? "",
      maxChars: llmsConfig.maxChars,
      sections: llmsConfig.sections,
      openapi: openapiDiscovery,
    } as any);
    llmsCacheByLocale.set(key, next);
    return next;
  }

  return {
    /**
     * GET handler — search, markdown, llms.txt, llms-full.txt, or skill.md.
     */
    async GET(request: Request) {
      const ctx = resolveContextFromRequest(request);
      const url = new URL(request.url);
      const devToolsRequest = resolveDevToolsRequest(url);
      if (devToolsRequest) {
        if (!devToolsEnabled || !isLocalDevToolsRequest(request)) {
          return createDevToolsUnavailableResponse();
        }

        if (devToolsRequest !== "page") {
          return Response.json(
            { error: "Method Not Allowed" },
            {
              status: 405,
              headers: { Allow: "POST" },
            },
          );
        }

        const requestedPath = getDevToolsRequestedPath(url, request);
        const page = getEditableDocsPage(ctx, requestedPath, root);
        if (!page) {
          return Response.json(
            { error: "Could not find an editable MDX page for this route." },
            { status: 404 },
          );
        }

        return Response.json(page, {
          headers: {
            "Cache-Control": "no-store",
            "X-Robots-Tag": "noindex",
          },
        });
      }

      if (resolveAgentSpecRequest(url)) {
        await emitDocsAnalyticsEvent(analytics, {
          type: "agent_spec_request",
          source: "server",
          url: request.url,
          path: url.pathname,
          locale: ctx.locale,
          properties: {
            method: "GET",
          },
        });
        return Response.json(
          buildAgentSpec({
            origin: url.origin,
            entry,
            i18n,
            search: searchConfig,
            mcp: mcpConfig,
            feedback: agentFeedbackConfig,
            llms: llmsConfig,
            sitemap: sitemapConfig,
            robots: robotsConfig,
            openapi: openapiDiscovery,
          }),
          {
            headers: {
              "Cache-Control": "public, max-age=0, s-maxage=3600",
              "X-Robots-Tag": "noindex",
            },
          },
        );
      }

      if (isApiReferenceOpenApiRequest(url)) {
        if (!openapiDiscovery.enabled) {
          return new Response("Not Found", {
            status: 404,
            headers: {
              "Content-Type": "text/plain; charset=utf-8",
              "X-Robots-Tag": "noindex",
            },
          });
        }

        const document = await buildApiReferenceOpenApiDocumentAsync(apiReferenceDocsConfig, {
          framework: "next",
          rootDir: root,
          baseUrl: url.origin,
        });

        return new Response(JSON.stringify(document, null, 2), {
          headers: {
            "Content-Type": "application/json; charset=utf-8",
            "Cache-Control": "public, max-age=0, s-maxage=3600",
            "X-Robots-Tag": "noindex",
          },
        });
      }

      const agentFeedbackRequest = resolveAgentFeedbackRequest(url, agentFeedbackConfig);

      if (agentFeedbackRequest) {
        if (agentFeedbackRequest.kind === "submit") {
          return Response.json(
            { error: "Method Not Allowed" },
            {
              status: 405,
              headers: {
                Allow: "POST",
              },
            },
          );
        }

        await emitDocsAnalyticsEvent(analytics, {
          type: "agent_feedback_schema",
          source: "server",
          url: request.url,
          path: url.pathname,
          locale: ctx.locale,
          properties: {
            method: "GET",
          },
        });
        return new Response(JSON.stringify(agentFeedbackConfig.schema, null, 2), {
          headers: {
            "Content-Type": "application/schema+json; charset=utf-8",
            "Cache-Control": "public, max-age=0, s-maxage=3600",
            "X-Robots-Tag": "noindex",
          },
        });
      }

      if (resolveAgentsRequest(url)) {
        await emitDocsAnalyticsEvent(analytics, {
          type: "agents_request",
          source: "server",
          url: request.url,
          path: url.pathname,
          locale: ctx.locale,
          properties: {
            method: "GET",
          },
        });
        return new Response(
          readRootAgentsDocument(root) ??
            renderAgentsDocument({
              origin: url.origin,
              entry,
              search: searchConfig,
              mcp: mcpConfig,
              feedback: agentFeedbackConfig,
              llms: llmsConfig,
              sitemap: sitemapConfig,
              robots: robotsConfig,
              openapi: openapiDiscovery,
            }),
          {
            headers: {
              "Content-Type": "text/markdown; charset=utf-8",
              "Cache-Control": "public, max-age=0, s-maxage=3600",
              "X-Robots-Tag": "noindex",
            },
          },
        );
      }

      if (resolveSkillRequest(url)) {
        await emitDocsAnalyticsEvent(analytics, {
          type: "skill_request",
          source: "server",
          url: request.url,
          path: url.pathname,
          locale: ctx.locale,
          properties: {
            method: "GET",
          },
        });
        return new Response(
          readRootSkillDocument(root) ??
            renderSkillDocument({
              origin: url.origin,
              entry,
              i18n,
              search: searchConfig,
              mcp: mcpConfig,
              feedback: agentFeedbackConfig,
              llms: llmsConfig,
              sitemap: sitemapConfig,
              robots: robotsConfig,
              openapi: openapiDiscovery,
            }),
          {
            headers: {
              "Content-Type": "text/markdown; charset=utf-8",
              "Cache-Control": "public, max-age=0, s-maxage=3600",
              "X-Robots-Tag": "noindex",
            },
          },
        );
      }

      const robotsResponse = createDocsRobotsResponse({
        request,
        entry,
        sitemap: sitemapConfig,
        baseUrl: llmsConfig.baseUrl ?? url.origin,
        robots: robotsConfig,
      });
      if (robotsResponse) return robotsResponse;

      const sitemapResponse = createDocsSitemapResponse({
        request,
        sitemap: sitemapConfig,
        entry,
        siteTitle: llmsConfig.siteTitle ?? "Documentation",
        baseUrl: llmsConfig.baseUrl ?? url.origin,
        pages: getIndexes(ctx),
        manifest: readDocsSitemapManifest(root, sitemapConfig),
      });
      if (sitemapResponse) return sitemapResponse;

      const markdownRequest =
        resolveMarkdownRequest(entry, url, request) ??
        resolvePublicMarkdownRequest(entry, docsPath, url, request);

      if (markdownRequest) {
        const document = await getMarkdownDocument(ctx, markdownRequest.requestedPath);
        const varyHeader = getDocsMarkdownVaryHeader(request);
        const canonicalLinkHeader = getPublicMarkdownCanonicalLinkHeader({
          origin: url.origin,
          ctx,
          requestedPath: markdownRequest.requestedPath,
        });

        if (!document) {
          await emitDocsAnalyticsEvent(analytics, {
            type: "agent_read",
            source: "server",
            url: request.url,
            path: url.pathname,
            locale: ctx.locale,
            properties: {
              requestedPath: markdownRequest.requestedPath,
              delivery: markdownRequest.delivery,
              found: false,
            },
          });
          await emitDocsAnalyticsEvent(analytics, {
            type: "markdown_request",
            source: "server",
            url: request.url,
            path: url.pathname,
            locale: ctx.locale,
            properties: {
              requestedPath: markdownRequest.requestedPath,
              delivery: markdownRequest.delivery,
              found: false,
            },
          });
          return new Response(
            renderDocsMarkdownNotFound({
              entry,
              requestedPath: markdownRequest.requestedPath,
              sitemap: sitemapConfig,
            }),
            {
              status: 404,
              headers: {
                "Content-Type": "text/markdown; charset=utf-8",
                ...(varyHeader ? { Vary: varyHeader } : {}),
                "X-Robots-Tag": "noindex",
              },
            },
          );
        }

        await emitDocsAnalyticsEvent(analytics, {
          type: "agent_read",
          source: "server",
          url: request.url,
          path: url.pathname,
          locale: ctx.locale,
          properties: {
            requestedPath: markdownRequest.requestedPath,
            delivery: markdownRequest.delivery,
            found: true,
            contentLength: document.length,
          },
        });
        await emitDocsAnalyticsEvent(analytics, {
          type: "markdown_request",
          source: "server",
          url: request.url,
          path: url.pathname,
          locale: ctx.locale,
          properties: {
            requestedPath: markdownRequest.requestedPath,
            delivery: markdownRequest.delivery,
            found: true,
            contentLength: document.length,
          },
        });
        return new Response(document, {
          headers: {
            "Content-Type": "text/markdown; charset=utf-8",
            "Cache-Control": "public, max-age=0, s-maxage=3600",
            Link: canonicalLinkHeader,
            ...(varyHeader ? { Vary: varyHeader } : {}),
            "X-Robots-Tag": "noindex",
          },
        });
      }

      const llmsRequest = resolveDocsLlmsTxtRequest(url, llmsConfig);
      if (llmsRequest) {
        if (!llmsConfig.enabled) {
          return new Response("Not Found", {
            status: 404,
            headers: {
              "Content-Type": "text/plain; charset=utf-8",
              "X-Robots-Tag": "noindex",
            },
          });
        }

        const selected = selectDocsLlmsTxtContent(getLlmsContent(ctx), llmsRequest);
        if (!selected) {
          return new Response("Not Found", {
            status: 404,
            headers: {
              "Content-Type": "text/plain; charset=utf-8",
              "X-Robots-Tag": "noindex",
            },
          });
        }

        const budgetIssue = getDocsLlmsTxtMaxCharsIssue(
          selected.label,
          selected.content,
          selected.maxChars,
        );
        if (budgetIssue?.mode === "error") {
          return new Response(budgetIssue.message, {
            status: 500,
            headers: {
              "Content-Type": "text/plain; charset=utf-8",
              "X-Robots-Tag": "noindex",
            },
          });
        }
        if (budgetIssue?.mode === "warn") {
          console.warn(`[docs] ${budgetIssue.message}`);
        }

        await emitDocsAnalyticsEvent(analytics, {
          type: "llms_request",
          source: "server",
          url: request.url,
          path: url.pathname,
          locale: ctx.locale,
          properties: {
            format: llmsRequest.format,
            section: llmsRequest.section?.route,
            contentLength: selected.content.length,
          },
        });
        return new Response(selected.content, {
          headers: {
            "Content-Type": "text/plain; charset=utf-8",
            "Cache-Control": "public, max-age=3600",
          },
        });
      }

      const query = url.searchParams.get("query")?.trim();
      if (!query) {
        return new Response(JSON.stringify([]), {
          headers: { "Content-Type": "application/json" },
        });
      }

      const searchStartedAt = Date.now();
      const results = await performDocsSearch({
        pages: getIndexes(ctx),
        query,
        search: resolveSearchRequestConfig(searchConfig, request.url),
        locale: ctx.locale,
        pathname: url.searchParams.get("pathname") ?? undefined,
        siteTitle: llmsConfig.siteTitle ?? "Documentation",
      });
      await emitDocsAnalyticsEvent(analytics, {
        type: "api_search",
        source: "server",
        url: request.url,
        path: url.pathname,
        locale: ctx.locale,
        input: { query },
        properties: {
          queryLength: query.length,
          resultCount: results.length,
          pathname: url.searchParams.get("pathname") ?? undefined,
          durationMs: Math.max(0, Date.now() - searchStartedAt),
        },
      });

      return new Response(JSON.stringify(results), {
        headers: { "Content-Type": "application/json" },
      });
    },

    /**
     * POST handler — AI chat with RAG.
     * Body: `{ messages: [{ role: "user", content: "How do I …?" }] }`
     * Response: SSE stream of chat completion chunks.
     */
    async POST(request: Request): Promise<Response> {
      const url = new URL(request.url);
      const devToolsRequest = resolveDevToolsRequest(url);
      if (devToolsRequest) {
        if (!devToolsEnabled || !isLocalDevToolsRequest(request)) {
          return createDevToolsUnavailableResponse();
        }

        if (devToolsRequest === "publish") {
          return handleDevToolsPublishRequest(request);
        }

        if (devToolsRequest === "theme") {
          return handleDevToolsThemeRequest(request, root);
        }

        const ctx = resolveContextFromRequest(request);

        if (devToolsRequest === "nav-item") {
          return handleDevToolsNavItemRequest(request, ctx, root);
        }

        const requestedPath = getDevToolsRequestedPath(url, request);
        const page = getEditableDocsPage(ctx, requestedPath, root);
        if (!page) {
          return Response.json(
            { error: "Could not find an editable MDX page for this route." },
            { status: 404 },
          );
        }

        const parsed = await parseDevToolsWriteBody(request);
        if (!parsed.ok) return parsed.response;

        const content = parsed.content.replace(/\r\n/g, "\n").replace(/\r/g, "\n");
        fs.writeFileSync(page.filePath, content.endsWith("\n") ? content : `${content}\n`);
        indexesByLocale.clear();
        llmsCacheByLocale.clear();
        markdownSourcesByLocale.clear();

        const stat = fs.statSync(page.filePath);
        return Response.json(
          {
            ok: true,
            requestedPath: page.requestedPath,
            relativePath: page.relativePath,
            lastModified: stat.mtime.toISOString(),
          },
          {
            headers: {
              "Cache-Control": "no-store",
              "X-Robots-Tag": "noindex",
            },
          },
        );
      }

      const agentFeedbackRequest = resolveAgentFeedbackRequest(url, agentFeedbackConfig);

      if (agentFeedbackRequest) {
        if (agentFeedbackRequest.kind === "schema") {
          return Response.json(
            { error: "Method Not Allowed" },
            {
              status: 405,
              headers: {
                Allow: "GET",
              },
            },
          );
        }

        const parsed = await parseAgentFeedbackData(request);
        if (!parsed.ok) {
          await emitDocsAnalyticsEvent(analytics, {
            type: "agent_feedback_error",
            source: "server",
            url: request.url,
            path: url.pathname,
            properties: {
              reason: "invalid_body",
            },
          });
          return parsed.response;
        }

        const payloadError = validateAgentFeedbackPayload(
          parsed.data.payload,
          agentFeedbackConfig.payloadSchema,
        );
        if (payloadError) {
          await emitDocsAnalyticsEvent(analytics, {
            type: "agent_feedback_error",
            source: "server",
            url: request.url,
            path: url.pathname,
            properties: {
              reason: "invalid_payload",
              error: payloadError,
            },
          });
          return Response.json({ error: payloadError }, { status: 400 });
        }

        if (!agentFeedbackConfig.onFeedback) {
          await emitDocsAnalyticsEvent(analytics, {
            type: "agent_feedback_submit",
            source: "server",
            url: request.url,
            path: url.pathname,
            properties: {
              handled: false,
              payloadKeys: Object.keys(parsed.data.payload),
              hasContext: Boolean(parsed.data.context),
            },
          });
          return Response.json({ ok: true, handled: false }, { status: 202 });
        }

        await agentFeedbackConfig.onFeedback(parsed.data);
        await emitDocsAnalyticsEvent(analytics, {
          type: "agent_feedback_submit",
          source: "server",
          url: request.url,
          path: url.pathname,
          properties: {
            handled: true,
            payloadKeys: Object.keys(parsed.data.payload),
            hasContext: Boolean(parsed.data.context),
          },
        });
        return Response.json({ ok: true, handled: true }, { status: 201 });
      }

      if (!aiConfig.enabled) {
        await emitDocsAnalyticsEvent(analytics, {
          type: "api_ai_error",
          source: "server",
          url: request.url,
          path: url.pathname,
          properties: {
            reason: "disabled",
          },
        });
        return Response.json(
          {
            error:
              "AI is not enabled. Set `ai: { enabled: true }` in your docs.config to enable it.",
          },
          { status: 404 },
        );
      }

      const ctx = resolveContextFromRequest(request);
      return handleAskAI(
        request,
        getIndexes(ctx),
        aiConfig,
        resolveAskAISearchRequestConfig({
          search: searchConfig,
          useMcp: aiConfig.useMcp,
          mcpEndpoint: mcpConfig.route,
          mcpEnabled: mcpConfig.enabled,
          mcpSearchEnabled: mcpConfig.tools.searchDocs,
          requestUrl: request.url,
        }),
        analytics,
        observability,
        {
          locale: ctx.locale,
        },
      );
    },
  };
}

/**
 * Create MCP route handlers for `/api/docs/mcp`.
 *
 * Returns `{ GET, POST, DELETE }` for use in a Next.js route handler.
 */
/**
 * @deprecated Prefer `createDocsMCPAPI` from `@farming-labs/next/api` in Next.js apps.
 * The `@farming-labs/theme/api` path is kept for compatibility and will be phased out.
 */
export function createDocsMCPAPI(options: DocsMCPAPIOptions = {}) {
  const rootDir = options.rootDir ?? process.cwd();
  const entry = options.entry ?? readEntry(rootDir);
  const appDir = getNextAppDir(rootDir);
  const contentDir = options.contentDir ?? path.join(appDir, entry);
  const navTitle =
    typeof options.nav?.title === "string" && options.nav.title.trim().length > 0
      ? options.nav.title
      : "Documentation";

  const source = createFilesystemDocsMcpSource({
    rootDir,
    entry,
    contentDir,
    siteTitle: navTitle,
    ordering: options.ordering,
  });

  const handlers = createDocsMcpHttpHandler({
    source,
    mcp: options.mcp ?? readMcpConfig(rootDir),
    search: options.search,
    analytics: options.analytics,
    observability: options.observability,
    defaultName: navTitle,
  });

  return {
    GET(request: Request) {
      return handlers.GET({ request });
    },
    POST(request: Request) {
      return handlers.POST({ request });
    },
    DELETE(request: Request) {
      return handlers.DELETE({ request });
    },
  };
}
