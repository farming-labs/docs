interface ChangelogSourceEntry {
  slug: string;
  date: string;
  sourceFile: string;
  metadata: Record<string, unknown>;
}

function resolveDocsContentDir(root: string, appDir: string, entry: string): string {
  const configuredContentDir = readDocsContentDir(root);
  if (!configuredContentDir) return join(root, appDir, entry);
  return isAbsolute(configuredContentDir) ? configuredContentDir : join(root, configuredContentDir);
}

function resolveChangelogContentDir(
  root: string,
  appDir: string,
  entry: string,
  changelogContentDir: string,
): string {
  if (isAbsolute(changelogContentDir)) return changelogContentDir;
  return join(resolveDocsContentDir(root, appDir, entry), changelogContentDir);
}

function findChangelogSourceEntries(changelogDir: string): ChangelogSourceEntry[] {
  if (!existsSync(changelogDir)) return [];

  const entries: ChangelogSourceEntry[] = [];
  for (const name of readdirSync(changelogDir).sort()) {
    if (!/^\d{4}-\d{2}-\d{2}$/.test(name)) continue;
    const entryDir = join(changelogDir, name);
    try {
      if (!statSync(entryDir).isDirectory()) continue;
    } catch {
      continue;
    }

    const sourceFile = ["page.mdx", "page.md"]
      .map((fileName) => join(entryDir, fileName))
      .find((value) => existsSync(value));

    if (!sourceFile) continue;

    let metadata: Record<string, unknown> = {};
    try {
      metadata = matter(readFileSync(sourceFile, "utf-8")).data;
    } catch {
      metadata = {};
    }

    entries.push({
      slug: name,
      date: name,
      sourceFile,
      metadata,
    });
  }

  return entries.sort((left, right) => right.date.localeCompare(left.date));
}

function extractObjectLiteral(content: string, key: string): string | undefined {
  const keyIndex = content.search(new RegExp(`${key}\\s*:\\s*\\{`));
  if (keyIndex === -1) return undefined;

  const braceStart = content.indexOf("{", keyIndex);
  if (braceStart === -1) return undefined;

  let depth = 0;

  for (let index = braceStart; index < content.length; index += 1) {
    const char = content[index];

    if (char === "{") {
      depth += 1;
      continue;
    }

    if (char !== "}") continue;

    depth -= 1;
    if (depth === 0) {
      return content.slice(braceStart + 1, index);
    }
  }

  return undefined;
}

function toImportPath(fromFile: string, toFile: string): string {
  const relativePath = relative(dirname(fromFile), toFile).replaceAll("\\", "/");
  return relativePath.startsWith(".") ? relativePath : `./${relativePath}`;
}

function buildChangelogManifestSource(
  root: string,
  manifestPath: string,
  entryPath: string,
  routePath: string,
  entries: ChangelogSourceEntry[],
): string {
  const imports: string[] = [];
  const records: string[] = [];

  entries.forEach((entry, index) => {
    const importName = `ChangelogEntry${index + 1}`;

    imports.push(`import ${importName} from "${toImportPath(manifestPath, entry.sourceFile)}";`);

    records.push(`  {
    slug: ${JSON.stringify(entry.slug)},
    date: ${JSON.stringify(entry.date)},
    url: ${JSON.stringify(`/${entryPath}/${routePath}/${entry.slug}`)},
    sourcePath: ${JSON.stringify(relative(root, entry.sourceFile).replaceAll("\\", "/"))},
    Component: ${importName},
    metadata: ${JSON.stringify(entry.metadata)},
  }`);
  });

  return `\
${GENERATED_BANNER}
import type { GeneratedChangelogEntry } from "@farming-labs/next/changelog";
${imports.join("\n")}

export const changelogEntries: GeneratedChangelogEntry[] = [
${records.join(",\n")}
];
`;
}

function resolveManagedChangelogSourceLayoutPath(
  root: string,
  appDir: string,
  entry: string,
  changelogContentDir: string,
): string | undefined {
  if (isAbsolute(changelogContentDir)) return undefined;

  const appRoot = join(root, appDir);
  const contentRoot = resolveChangelogContentDir(root, appDir, entry, changelogContentDir);
  const relativePath = relative(appRoot, contentRoot);

  if (relativePath.startsWith("..") || isAbsolute(relativePath)) return undefined;
  return join(contentRoot, "layout.tsx");
}

function removeManagedFile(filePath: string) {
  if (isManagedGeneratedFile(filePath)) {
    rmSync(filePath, { force: true });
  }
}

function readMcpConfig(root: string): {
  enabled: boolean;
  route: string;
} {
  for (const ext of FILE_EXTS) {
    const configPath = join(root, `docs.config.${ext}`);
    if (!existsSync(configPath)) continue;

    try {
      const content = readFileSync(configPath, "utf-8");

      if (content.match(/mcp\s*:\s*false/)) {
        return { enabled: false, route: "/api/docs/mcp" };
      }

      if (content.match(/mcp\s*:\s*true/)) {
        return { enabled: true, route: "/api/docs/mcp" };
      }

      const block = extractObjectLiteral(content, "mcp");
      if (!block) continue;

      const enabledMatch = block.match(/enabled\s*:\s*(true|false)/);
      const routeMatch = block.match(/route\s*:\s*["']([^"']+)["']/);

      return {
        enabled: enabledMatch ? enabledMatch[1] !== "false" : true,
        route: normalizeRoutePath(routeMatch?.[1] ?? "/api/docs/mcp"),
      };
    } catch {
      return { enabled: false, route: "/api/docs/mcp" };
    }
  }

  return { enabled: false, route: "/api/docs/mcp" };
}

function normalizeRoutePath(route: string): string {
  const normalized = `/${route}`.replace(/\/+/g, "/");
  return normalized !== "/" ? normalized.replace(/\/+$/, "") : "/api/docs/mcp";
}

// ─── withDocs ───────────────────────────────────────────────────────

export function withDocs(nextConfig: Record<string, unknown> = {}) {
  const root = process.cwd();
  const workspaceRoot = findDocsWorkspaceRoot(root);
  const docsConfigPath = readDocsConfigPath(root);
  const docsConfigAbsolutePath = join(root, docsConfigPath);
  const docsConfigRelativeAlias =
    docsConfigPath.startsWith("./") || docsConfigPath.startsWith("../")
      ? docsConfigPath
      : `./${docsConfigPath}`;

  // ── 1. Auto-generate mdx-components.tsx if missing ──────────────
  if (!hasFile(root, "mdx-components")) {
    writeFileSync(join(root, "mdx-components.tsx"), MDX_COMPONENTS_TEMPLATE);
  }

  // ── 2. Auto-generate app/{entry}/layout.tsx if missing (or src/app when using src dir) ──
  const entry = readDocsEntry(root);
  const configuredContentDir = readDocsContentDir(root);
  const appDir = getNextAppDir(root);
  const docsContentDir = configuredContentDir ?? join(appDir, entry);
  const layoutDir = join(root, appDir, entry);
  if (!existsSync(layoutDir)) {
    mkdirSync(layoutDir, { recursive: true });
  }

  const docsLayoutPath = join(layoutDir, "layout.tsx");
  if (!hasFile(layoutDir, "layout") || isManagedGeneratedFile(docsLayoutPath)) {
    writeFileSync(join(layoutDir, "layout.tsx"), DOCS_LAYOUT_TEMPLATE);
  }

  // ── 3. Auto-generate app/api/docs/route.ts if missing (skip for static export) ──
  // With output: 'export' (Cloudflare Pages, etc.) there is no server; set ai.enabled: false
  // and use client-side search or leave search disabled.
  const isStaticExport = nextConfig.output === "export";
  const docsApiRouteDir = join(root, appDir, "api", "docs");
  if (!isStaticExport && !hasFile(docsApiRouteDir, "route")) {
    mkdirSync(docsApiRouteDir, { recursive: true });
    writeFileSync(join(docsApiRouteDir, "route.ts"), DOCS_API_ROUTE_TEMPLATE);
  }

  const mcp = readMcpConfig(root);
  const docsMcpRouteDir = join(root, appDir, "api", "docs", "mcp");
  if (
    mcp.enabled &&
    mcp.route === "/api/docs/mcp" &&
    !isStaticExport &&
    !hasFile(docsMcpRouteDir, "route")
  ) {
    mkdirSync(docsMcpRouteDir, { recursive: true });
    writeFileSync(join(docsMcpRouteDir, "route.ts"), DOCS_MCP_ROUTE_TEMPLATE);
  }

  // ── 3.1. Auto-generate API reference route/page ───────────────────
  const apiReference = readApiReferenceConfig(root);
  if (apiReference.enabled && !isStaticExport) {
    const apiReferenceBaseDir = join(root, appDir, ...apiReference.path.split("/"));
    const apiReferencePageDir = join(apiReferenceBaseDir, "[[...slug]]");
    const apiReferencePagePath = join(apiReferencePageDir, "page.tsx");
    const apiReferenceLayoutPath = join(apiReferenceBaseDir, "layout.tsx");
    const apiReferenceRouteDir = join(apiReferenceBaseDir, "[[...slug]]");
    const apiReferenceRoutePath = join(apiReferenceRouteDir, "route.ts");
    const legacyApiReferencePagePath = join(apiReferenceBaseDir, "page.tsx");

    if (apiReference.renderer === "fumadocs") {
      removeManagedFile(apiReferenceRoutePath);
      removeManagedFile(legacyApiReferencePagePath);
      if (
        !hasFile(apiReferenceBaseDir, "layout") ||
        isManagedGeneratedFile(apiReferenceLayoutPath)
      ) {
        mkdirSync(apiReferenceBaseDir, { recursive: true });
        writeFileSync(apiReferenceLayoutPath, API_REFERENCE_LAYOUT_TEMPLATE);
      }
      if (!hasFile(apiReferencePageDir, "page") || isManagedGeneratedFile(apiReferencePagePath)) {
        mkdirSync(apiReferencePageDir, { recursive: true });
        writeFileSync(apiReferencePagePath, API_REFERENCE_PAGE_TEMPLATE);
      }
    } else {
      removeManagedFile(apiReferenceLayoutPath);
      removeManagedFile(apiReferencePagePath);
      if (
        !hasFile(apiReferenceRouteDir, "route") ||
        isManagedGeneratedFile(apiReferenceRoutePath)
      ) {
        mkdirSync(apiReferenceRouteDir, { recursive: true });
        writeFileSync(apiReferenceRoutePath, API_REFERENCE_ROUTE_TEMPLATE);
      }
    }
  }

  const changelog = readChangelogConfig(root);
  if (changelog.enabled) {
    const changelogBaseDir = join(root, appDir, entry, ...changelog.path.split("/"));
    const changelogEntryDir = join(changelogBaseDir, "[slug]");
    const changelogIndexPath = join(changelogBaseDir, "page.tsx");
    const changelogEntryPath = join(changelogEntryDir, "page.tsx");
    const changelogManifestPath = join(changelogBaseDir, CHANGELOG_MANIFEST_FILENAME);
    const changelogSourceDir = resolveChangelogContentDir(root, appDir, entry, changelog.contentDir);
    const changelogUsesSourcePagesAsDetails =
      !isAbsolute(changelog.contentDir) && changelog.contentDir === changelog.path;
    const changelogEntries = findChangelogSourceEntries(changelogSourceDir);
    const changelogSourceLayoutPath = resolveManagedChangelogSourceLayoutPath(
      root,
      appDir,
      entry,
      changelog.contentDir,
    );
    const legacyChangelogBaseDirs = new Set<string>([
      join(root, appDir, ...changelog.path.split("/")),
      ...(isAbsolute(changelog.contentDir) ? [] : [join(root, appDir, ...changelog.contentDir.split("/"))]),
    ]);

    for (const legacyChangelogBaseDir of legacyChangelogBaseDirs) {
      if (legacyChangelogBaseDir === changelogBaseDir) continue;

      removeManagedFile(join(legacyChangelogBaseDir, "[slug]", "page.tsx"));
      removeManagedFile(join(legacyChangelogBaseDir, "page.tsx"));
      removeManagedFile(join(legacyChangelogBaseDir, CHANGELOG_MANIFEST_FILENAME));
    }

    mkdirSync(changelogBaseDir, { recursive: true });
    writeFileSync(
      changelogManifestPath,
      buildChangelogManifestSource(
        root,
        changelogManifestPath,
        entry,
        changelog.path,
        changelogEntries,
      ),
    );

    if (!hasFile(changelogBaseDir, "page") || isManagedGeneratedFile(changelogIndexPath)) {
      writeFileSync(changelogIndexPath, CHANGELOG_INDEX_PAGE_TEMPLATE);
    }

    if (changelogUsesSourcePagesAsDetails) {
      removeManagedFile(changelogEntryPath);
      if (changelogSourceLayoutPath) {
        removeManagedFile(changelogSourceLayoutPath);
      }
    } else {
      if (!hasFile(changelogEntryDir, "page") || isManagedGeneratedFile(changelogEntryPath)) {
        mkdirSync(changelogEntryDir, { recursive: true });
        writeFileSync(changelogEntryPath, CHANGELOG_ENTRY_PAGE_TEMPLATE);
      }

      if (changelogSourceLayoutPath) {
        const changelogSourceLayoutDir = dirname(changelogSourceLayoutPath);
        if (
          !hasFile(changelogSourceLayoutDir, "layout") ||
          isManagedGeneratedFile(changelogSourceLayoutPath)
        ) {
          mkdirSync(changelogSourceLayoutDir, { recursive: true });
          writeFileSync(changelogSourceLayoutPath, CHANGELOG_SOURCE_LAYOUT_TEMPLATE);
        }
      }
    }
  }

  // ── 4. Configure MDX compilation ────────────────────────────────
  const ogEndpoint = readOgEndpoint(root);
  const remarkPlugins: MdxPluginEntry[] = ["remark-gfm", "remark-frontmatter"];
  if (ogEndpoint) {
    remarkPlugins.push(["@farming-labs/next/mdx-plugins/remark-og", { endpoint: ogEndpoint }]);
  }
  remarkPlugins.push(
    ["remark-mdx-frontmatter", { name: "metadata" }],
    "@farming-labs/next/mdx-plugins/remark-heading",
  );
  const rehypePlugins: MdxPluginEntry[] = [
    "@farming-labs/next/mdx-plugins/rehype-toc",
    [
      "@farming-labs/next/mdx-plugins/rehype-code",
      { themes: { dark: "github-dark", light: "github-light" } },
    ],
  ];

  const withMDX = createMDX({
    extension: /\.mdx?$/,
    options: {
      remarkPlugins,
      rehypePlugins,
    },
  });

  // ── 5. Ensure pageExtensions includes md/mdx ───────────────────
  const defaultExts = ["js", "jsx", "md", "mdx", "ts", "tsx"];
  const userExts = nextConfig.pageExtensions as string[] | undefined;

  if (userExts) {
    for (const ext of ["md", "mdx"]) {
      if (!userExts.includes(ext)) {
        userExts.push(ext);
      }
    }
  } else {
    nextConfig.pageExtensions = defaultExts;
  }

  const existingTurbopack = (nextConfig.turbopack as Record<string, unknown> | undefined) ?? {};
  const existingResolveAlias =
    (existingTurbopack.resolveAlias as Record<string, string> | undefined) ?? {};

  nextConfig.turbopack = {
    ...existingTurbopack,
    ...(workspaceRoot && !existingTurbopack.root ? { root: workspaceRoot } : {}),
    resolveAlias: {
      ...(workspaceRoot ? createDocsWorkspaceAliases() : {}),
      ...existingResolveAlias,
      [INTERNAL_DOCS_CONFIG_ALIAS]: docsConfigRelativeAlias,
      "fumadocs-openapi": toTurbopackAliasPath(root, FUMADOCS_OPENAPI_PACKAGE_ALIAS),
      "fumadocs-openapi/ui": toTurbopackAliasPath(root, FUMADOCS_OPENAPI_UI_ALIAS),
      "fumadocs-openapi/server": toTurbopackAliasPath(root, FUMADOCS_OPENAPI_SERVER_ALIAS),
      "fumadocs-core": toTurbopackAliasPath(root, FUMADOCS_CORE_PACKAGE_ALIAS),
      "fumadocs-core/framework": toTurbopackAliasPath(root, FUMADOCS_CORE_FRAMEWORK_ALIAS),
      "fumadocs-core/framework/next": toTurbopackAliasPath(
        root,
        FUMADOCS_CORE_FRAMEWORK_NEXT_ALIAS,
      ),
      "fumadocs-ui": toTurbopackAliasPath(root, FUMADOCS_UI_PACKAGE_ALIAS),
      "fumadocs-ui/layouts/notebook": toTurbopackAliasPath(root, FUMADOCS_UI_NOTEBOOK_ALIAS),
      "fumadocs-ui/layouts/notebook/page": toTurbopackAliasPath(
        root,
        FUMADOCS_UI_NOTEBOOK_PAGE_ALIAS,
      ),
      "fumadocs-ui/provider/next": toTurbopackAliasPath(root, FUMADOCS_UI_PROVIDER_NEXT_ALIAS),
    },
  };

  const userWebpack = nextConfig.webpack as
    | ((config: Record<string, any>, options: Record<string, any>) => Record<string, any>)
    | undefined;

  nextConfig.webpack = (config: Record<string, any>, options: Record<string, any>) => {
    const resolvedConfig = userWebpack ? userWebpack(config, options) : config;
    resolvedConfig.resolve ??= {};
    resolvedConfig.resolve.alias ??= {};
    if (workspaceRoot) {
      Object.assign(resolvedConfig.resolve.alias, {
        "@farming-labs/docs$": join(workspaceRoot, "packages", "docs", "dist", "index.mjs"),
        "@farming-labs/docs/server": join(
          workspaceRoot,
          "packages",
          "docs",
          "dist",
          "server.mjs",
        ),
        "@farming-labs/next$": join(workspaceRoot, "packages", "next", "dist", "index.mjs"),
        "@farming-labs/next/api": join(workspaceRoot, "packages", "next", "dist", "api.mjs"),
        "@farming-labs/next/changelog": join(
          workspaceRoot,
          "packages",
          "next",
          "dist",
          "changelog.mjs",
        ),
        "@farming-labs/next/client-callbacks": join(
          workspaceRoot,
          "packages",
          "next",
          "dist",
          "client-callbacks.mjs",
        ),
        "@farming-labs/next/layout": join(
          workspaceRoot,
          "packages",
          "next",
          "dist",
          "layout.mjs",
        ),
        "@farming-labs/theme$": join(
          workspaceRoot,
          "packages",
          "fumadocs",
          "dist",
          "index.mjs",
        ),
        "@farming-labs/theme/api": join(
          workspaceRoot,
          "packages",
          "fumadocs",
          "dist",
          "docs-api.mjs",
        ),
      });
    }
    resolvedConfig.resolve.alias[INTERNAL_DOCS_CONFIG_ALIAS] = docsConfigAbsolutePath;
    resolvedConfig.resolve.alias["fumadocs-openapi"] = FUMADOCS_OPENAPI_PACKAGE_ALIAS;
    resolvedConfig.resolve.alias["fumadocs-openapi/ui"] = FUMADOCS_OPENAPI_UI_ALIAS;
    resolvedConfig.resolve.alias["fumadocs-openapi/server"] = FUMADOCS_OPENAPI_SERVER_ALIAS;
    resolvedConfig.resolve.alias["fumadocs-core"] = FUMADOCS_CORE_PACKAGE_ALIAS;
    resolvedConfig.resolve.alias["fumadocs-core/framework"] = FUMADOCS_CORE_FRAMEWORK_ALIAS;
    resolvedConfig.resolve.alias["fumadocs-core/framework/next"] =
      FUMADOCS_CORE_FRAMEWORK_NEXT_ALIAS;
    resolvedConfig.resolve.alias["fumadocs-ui"] = FUMADOCS_UI_PACKAGE_ALIAS;
    resolvedConfig.resolve.alias["fumadocs-ui/layouts/notebook"] = FUMADOCS_UI_NOTEBOOK_ALIAS;
    resolvedConfig.resolve.alias["fumadocs-ui/layouts/notebook/page"] =
      FUMADOCS_UI_NOTEBOOK_PAGE_ALIAS;
    resolvedConfig.resolve.alias["fumadocs-ui/provider/next"] = FUMADOCS_UI_PROVIDER_NEXT_ALIAS;
    return resolvedConfig;
  };

  const existingTracingIncludes =
    (nextConfig.outputFileTracingIncludes as Record<string, string[] | undefined> | undefined) ??
    {};
  const docsTraceGlob = docsContentDir.replace(/\\/g, "/").replace(/^\.?\//, "") + "/**/*";

  nextConfig.outputFileTracingIncludes = {
    ...existingTracingIncludes,
    "/api/docs": [...new Set([...(existingTracingIncludes["/api/docs"] ?? []), docsTraceGlob])],
    "/api/docs/mcp": [
      ...new Set([...(existingTracingIncludes["/api/docs/mcp"] ?? []), docsTraceGlob]),
    ],
  };

  return withMDX(nextConfig);
}
