function hasFile(root: string, baseName: string): boolean {
  return FILE_EXTS.some((ext) => existsSync(join(root, `${baseName}.${ext}`)));
}

function isManagedGeneratedFile(filePath: string): boolean {
  if (!existsSync(filePath)) return false;

  try {
    return readFileSync(filePath, "utf-8").startsWith(GENERATED_BANNER);
  } catch {
    return false;
  }
}

/** Read the docs entry path from docs.config.ts[x] (defaults to "docs"). */
function readDocsEntry(root: string): string {
  for (const ext of FILE_EXTS) {
    const configPath = join(root, `docs.config.${ext}`);
    if (existsSync(configPath)) {
      try {
        const content = readFileSync(configPath, "utf-8");
        const match = content.match(/entry\s*:\s*["']([^"']+)["']/);
        if (match) return match[1];
      } catch {
        // fall through to default
      }
    }
  }
  return "docs";
}

function readDocsContentDir(root: string): string | undefined {
  for (const ext of FILE_EXTS) {
    const configPath = join(root, `docs.config.${ext}`);
    if (!existsSync(configPath)) continue;

    try {
      const content = readFileSync(configPath, "utf-8");
      const match = content.match(/^[ \t]{0,2}contentDir\s*:\s*["']([^"']+)["']/m);
      if (match?.[1]) return match[1];
    } catch {
      // fall through
    }
  }

  return undefined;
}

function readDocsConfigPath(root: string): string {
  for (const ext of FILE_EXTS) {
    const relativePath = `docs.config.${ext}`;
    if (existsSync(join(root, relativePath))) return relativePath;
  }

  return "docs.config.ts";
}

/** Read the OG endpoint from docs.config.ts[x] (returns undefined if not set). */
function readOgEndpoint(root: string): string | undefined {
  for (const ext of FILE_EXTS) {
    const configPath = join(root, `docs.config.${ext}`);
    if (existsSync(configPath)) {
      try {
        const content = readFileSync(configPath, "utf-8");
        const match = content.match(/endpoint\s*:\s*["']([^"']+)["']/);
        if (match && match[1]) return match[1];
      } catch {
        // fall through
      }
    }
  }
  return undefined;
}

function readApiReferenceConfig(root: string): {
  enabled: boolean;
  path: string;
  renderer: "fumadocs" | "scalar";
  routeRoot: string;
} {
  for (const ext of FILE_EXTS) {
    const configPath = join(root, `docs.config.${ext}`);
    if (!existsSync(configPath)) continue;

    try {
      const content = readFileSync(configPath, "utf-8");

      const directFalse = content.match(/apiReference\s*:\s*false/);
      if (directFalse) {
        return { enabled: false, path: "api-reference", renderer: "fumadocs", routeRoot: "api" };
      }

      const directTrue = content.match(/apiReference\s*:\s*true/);
      if (directTrue) {
        return { enabled: true, path: "api-reference", renderer: "fumadocs", routeRoot: "api" };
      }

      const block = extractObjectLiteral(content, "apiReference");
      if (!block) continue;

      const enabledMatch = block.match(/enabled\s*:\s*(true|false)/);
      const pathMatch = block.match(/path\s*:\s*["']([^"']+)["']/);
      const rendererMatch = block.match(/renderer\s*:\s*["'](fumadocs|scalar)["']/);
      const routeRootMatch = block.match(/routeRoot\s*:\s*["']([^"']+)["']/);

      return {
        enabled: enabledMatch ? enabledMatch[1] !== "false" : true,
        path: pathMatch?.[1]?.replace(/^\/+|\/+$/g, "") || "api-reference",
        renderer: (rendererMatch?.[1] as "fumadocs" | "scalar" | undefined) ?? "fumadocs",
        routeRoot: routeRootMatch?.[1]?.replace(/^\/+|\/+$/g, "") || "api",
      };
    } catch {
      return { enabled: false, path: "api-reference", renderer: "fumadocs", routeRoot: "api" };
    }
  }

  return { enabled: false, path: "api-reference", renderer: "fumadocs", routeRoot: "api" };
}

